The amounts referred to in paragraph 1 of this article shall be exclusive of legal or other costs
incurred by the parties in asserting their rights, payment or reimbursement of which may devolve on the
carrier, and of interest calculated in conformity with the law judged applicable by the court seized of the
case.
3.
Higher limits may be agreed between the parties to the contract of carriage.
Section 3. General Provisions
Article 17
1.
The carrier shall be exonerated wholly or in part from his liability under this Convention to the
extent that loss or damage results from the wrongful act or neglect of the passenger or from conduct by
the passenger not conforming to the normal conduct of a passenger.
2.
Where the carrier is liable for the loss or damage but a third party has contributed thereto by his
acts or omissions, the carrier shall be liable for the whole of such loss or damage without prejudice to any
right or recourse he may have against such third party.
3.
The carrier shall not be liable under this Convention for loss or damage caused by a nuclear
incident when pursuant to special provisions in force in a Contracting State governing liability in the field
of nuclear energy the operator of a nuclear installation or another person who is substituted for him is
liable for the loss or damage.
Article 18
1.
In all cases governed by this Convention, proceedings for liability on any grounds whatever may
not be instituted against the carrier or against persons for whom he is responsible under article 4
otherwise than on the terms and within the limits laid down in this Convention.
2.
If the loss or damage results from willful misconduct or gross negligence by the carrier or a person
for whom he is responsible under article 4, the carrier shall not be entitled to avail himself of the
provisions of this Convention which exclude his liability wholly or in part or which limit the compensation
payable. The same rule shall apply to a person for whom the carrier is responsible under article 4 if that
person''s liability is involved and the loss or damage results from willful misconduct or gross negligence by
that person.
3.
Subject to the provisions of the preceding paragraph, the total amount of compensation payable by
the carrier and by the persons for whom the carrier is responsible under article 4 shall not exceed the
amounts prescribed in the preceding sections of this chapter.
Article 19
The franc referred to in this Convention shall be the gold franc weighing 10/31 of a gramme and
being of millesimal fineness 900.
