5.
Security for costs of proceedings arising out of carriage under this Convention shall not be required
from nationals of Contracting States who have their residence or a place of business in one of those
States.
Article 22
1.
The period of limitation for actions arising out of the death or wounding of or out of any other
bodily or mental injury to a passenger shall be three years.
The period of limitation shall run from the date on which the person suffering the loss or damage
had or should have had knowledge of it. However, the period of limitation shall not exceed five years
from the date of the accident.
2.
The period of limitation for actions arising out of carriage under this Convention other than those
referred to in paragraph 1 of this article shall in all cases be one year.
The period of limitation shall run from the date on which the vehicle arrived at the place of
destination of the passenger or, in the case of non-arrival, from the date on which the vehicle ought to
have arrived at the place of destination of the passenger.
3.
A written claim shall suspend the period of limitation until the date on which the carrier rejects the
claim by notification in writing and returns any documents handed to him in support of the claim. If a part
of the claim is admitted, the period of limitation shall start to run again only in respect of that part of the
claim which is still in dispute. The burden of proof of the receipt of the claim or of the reply and of the
return of the documents shall rest with the party relying upon those facts. Further claims having the same
object shall not suspend the running of the period of limitation unless the carrier agrees to consider them.
4.
Subject to the provisions of the preceding paragraph, the extension of the period of limitation shall
be governed by the provisions of the law of the court or tribunal seized of the case not including the rules
relating to conflict of laws. That law shall also govern the fresh accrual of rights of action.
Chapter VI
NULLITY OF STIPULATIONS CONTRARY TO THE CONVENTION
Article 23
1.
Any stipulation which would directly or indirectly derogate from the provisions of this Convention
shall be null and void. The nullity of such a stipulation shall not involve the nullity of the other provisions
of the contract.
2.
In particular, any clause assigning to the carrier the benefit of any insurance made in favour of the
passenger or any similar clause, or any clause shifting the burden of proof, shall be null and void.
3.
Any clauses assigning to an arbitral tribunal a jurisdiction which is stipulated before the event that
caused the damage shall likewise be null and void.
