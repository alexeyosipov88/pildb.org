Where in respect of a claim to which paragraph 1 of this article applies an action is pending
before a court or tribunal competent under this paragraph or where in respect of such a claim judgement
has been entered by such a court or tribunal, no new action shall be started on the same grounds
between the same parties unless the judgement of the court or tribunal before which the first action was
brought is not enforceable in the country in which the fresh proceedings are brought.
3.
Where a judgement entered by a court or tribunal of a Contracting State, the said court or
