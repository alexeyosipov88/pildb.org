Section 2. Luggage
Article 8
1.
The carrier may, and at the request of the passenger shall, issue a luggage registration voucher
indicating the number and nature of the pieces of luggage handed over to him. The voucher shall be
issued free, subject to the collection of any taxes payable on it.
2.
The luggage registration voucher, if it is not combined with a ticket, shall state the name and the
address of the carrier and shall contain the statement that the contract is subject, any clause to the
contrary notwithstanding, to the provisions of this Convention.
3.
The carrier shall be liable for any damage which may be caused to the passengers by a breach of
the obligations placed upon him by this article.
Article 9
The luggage registration voucher shall be prima facie evidence of the particulars shown on it and,
subject to such reservations as may be entered on it, that the luggage appeared to be in good condition
when handed over.
Article 10
1.
A carrier acting in good faith shall be deemed to have made a valid delivery of the luggage if he
delivers the luggage to the holder of the luggage registration voucher.
2.
If the luggage registration voucher is not produced, the carrier shall not be obliged to deliver the
luggage covered by it unless the person claiming the luggage can prove his right thereto; should the
evidence appear insufficient, the carrier may require adequate security for the luggage and such security
shall be returned within a period of one year from the date on which it was paid over.
3.
Luggage which is not claimed on the arrival of the vehicle, whether or not a voucher has been
issued, shall be deposited in a safe and convenient place; such luggage shall be deemed to be in the
custody of the carrier on behalf of the passenger; such custody may be entrusted to a third party; the
person having custody shall be entitled to a fair remuneration. All other matters connected with the
deposit of unclaimed luggage shall be governed by the law of the place where the luggage has been
deposited.
Chapter IV
LIABILITY OF THE CARRIER
Section 1. Personal Injuries
Article 11
1.
The carrier shall be liable for loss or damage resulting from the death or wounding of or from any
other doily or mental injury caused to a passenger as a result of an accident connected with the carriage
and occurring while the passenger is inside the vehicle or is entering or alighting from the vehicle, or
occurring in connexion with the loading or unloading of luggage.
2.
The carrier shall be relieved of this liability if the accident was caused by circumstances which a
carrier, using the diligence which the particular facts of the case called for, could not have avoided and
the consequences of which he was unable to prevent.
3.
The carrier shall not be relieved of liability by reason of any physical or mental failing of the driver,
of any defect in, or malfunctioning of, the vehicle, or of any wrongful act or neglect on the part of the
person from whom he may have hired the vehicle or of the persons for whom the latter would have been
responsible, in accordance with the provisions of article 4, if he had himself been the carrier.
Article 12
Subject to article 13, paragraph 1, the court or tribunal seized of the case shall determine in
accordance with the national law of the place where the said court or tribunal is situated, including the
rules relating to conflict of laws, the extent of the injury giving rise to compensation as a result of the death
or wounding of or any other doily or mental injury caused to a passenger as well as what persons are
entitled to compensation for such injury.
Article 13
1.
The total damages payable by the carrier in respect of the same occurrence shall not exceed
250,000 francs for each victim. Any Contracting State may, however, set a higher limit or set no limit at
all. When the carrier has his principal establishment in such a State, or in a non-contracting State whose
legislation provides for a higher limit or does not provide for a limit at all, the law of that State, not
including the rules relating to conflict of laws, shall apply for the determination of the total amount.
2.
The amount referred to in paragraph 1 of this article shall be exclusive of legal or other costs
incurred by the parties in asserting their rights, payment or reimbursement of which may devolve on the
carrier, and of interest calculated in conformity with the law judged applicable by the court seized of the
case.
3.
A higher limit may be agreed between the parties to the contract of carriage. Unless the contract
provides otherwise, such agreement shall be for the benefit of all persons entitled to compensation.
