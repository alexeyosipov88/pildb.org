Chapter VII
FINAL PROVISIONS
Article 24
1.
This Convention is open for signature or accession by States members of the Economic
Commission for Europe and States admitted to the Commission in a consultative capacity under
paragraph 8 of the Commission''s Terms of Reference.
2.
Such States as may participate in certain activities of the Economic Commission for Europe in
accordance with paragraph 11 of the Commission''s Terms of Reference may become Contracting
Parties to this Convention by acceding thereto after its entry into force.
3.
The Convention shall be open for signature until 1 March 1974 inclusive. Thereafter, it shall be
open for accession.
4.
This Convention shall be subject to ratification by the signatory States.
5.
The instruments of ratification or accession shall be deposited with the Secretary-General of the
United Nations.
Article 25
1.
This Convention shall come into force on the ninetieth day after five of the States referred to in
article 24, paragraph 1, have deposited their instruments of ratification or accession.
2.
With respect to any State which ratifies or accedes to this Convention after five States have
deposited their instruments of ratification or accession, the Convention shall enter into force on the
ninetieth day after the said State has deposited its instrument of ratification or accession.
Article 26
1.
Any Contracting Party may denounce this Convention by giving notice of denunciation to the
Secretary-General of the United Nations.
2.
The denunciation shall take effect twelve months after the date on which the Secretary-General
received the notice of denunciation.
Article 27
If, after the entry into force of this Convention, the number of Contracting Parties is reduced, as a
result of denunciations, to less than five, the Convention shall cease to be in force from the date on which
the last of such denunciations takes effect.
Article 28
1.
Any State may, at the time of depositing its instrument of ratification or accession, or at any time
thereafter, declare by notice addressed to the Secretary-General of the United Nations that the
