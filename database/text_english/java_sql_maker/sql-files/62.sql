under an individual or collective contract of carriage to carry one or more persons and,
where appropriate, their luggage, whether or not he performs the carriage himself;
passenger means any person who, in the performance of a contract of carriage made by
him or on his behalf, is carried either for reward or gratuitously by a carrier;
vehicle means any motor vehicle used in the performance of a contract of carriage and
intended for the carriage of persons; a trailer shall be deemed to form part of the vehicle.
This Convention shall also apply where the carrier is a State of other public body.
4.
The Contracting States undertake not to vary any of the provisions of this Convention by special
agreements between two or more of them, except to make it inapplicable to their frontier traffic.
Article 2
Where carriage by road is interrupted and another mode of transport is used, this Convention shall
nevertheless apply to the portions of carriage which are performed by road, even if they are not
international within the meaning of article 1, provided that they are not ancillary to the other mode of
transport.
Article 3
Where the vehicle itself is carried over part of the journey by another mode of transport, this
Convention shall apply to loss or damage caused by an incident connected with the carriage by the
vehicle and which occurs either while the passenger is inside the vehicle or is entering or alighting from the
vehicle, or in connexion with the fact that luggage is on or in the vehicle or is being loaded or unloaded.
Chapter II
PERSONS FOR WHOM THE CARRIER IS RESPONSIBLE
Article 4
For the purposes of this Convention the carrier shall be responsible for the acts and omissions of
his agents and servants and of all other persons of whose services he makes use for the performance of
the obligations which are incumbent upon him by virtue of the contract of carriage by road when such
agents, servants or other persons are acting within the scope of their employment, as if such acts or
omissions were his own.
Chapter III
TRANSPORT DOCUMENTS
Section 1. Passengers
Article 5
1.
Where passengers are carried, the carrier shall issue an individual or a collective ticket.
The absence, irregularity or loss of the ticket shall not affect the existence or the validity of the contract
of carriage, which shall remain subject to the provisions of this Convention.
2.
The ticket shall show the name and the address of the carrier and shall contain the statement that
the contract is subject, any clause to the contrary notwithstanding, to the provisions of this Convention.
3.
The carrier shall be liable for any damage which may be caused to the passengers by a breach of
the obligations placed upon him by this article.
Article 6
1.
The ticket shall be prima facie evidence of the particulars shown on it.
Article 7
Unless the ticket contains a provision to the contrary it shall, if it is not made out in the passenger''s
name, be transferable at any time before the journey begins.
