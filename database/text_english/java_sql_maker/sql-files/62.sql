Chapter V
CLAIM AND ACTIONS
Article 20
1.
The receipt of luggage by a passenger without complaint on his part shall be prima facie evidence
that the luggage was delivered complete and in good condition. The complaint must be made to the
carrier either orally or in writing within the seven days next following actual receipt of the luggage by the
complainant.
In the case of luggage not handed over to the carrier, the period specified above shall run from
the time when the loss or damage is noticed, and from not later than the time of arrival of the vehicle at
the place of destination of the passenger.
2.
The passenger shall be relieved of the obligations imposed on him by paragraph 1 of this article if
the loss or condition of the luggage has been duly checked by the passenger and the carrier.
