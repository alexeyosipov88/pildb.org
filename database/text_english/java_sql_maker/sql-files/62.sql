ON THE CONTRACT FOR THE INTERNATIONAL
CARRIAGE OF PASSGENGERS AND LUGGAGE BY ROAD
(CVR)
THE CONTRACTING PARTIES,
HAVING RECOGNIZED the desirability of standardizing the conditions governing the contract for the
international carriage of passengers and luggage by road,
HAVE AGREED as follows:
CHAPTER I
SCOPE OF APPLICATION
Article 1
1.
This Convention shall apply to every contract for the carriage of passengers, and, where
appropriate, of their luggage in vehicles by road when the contract provides that the carriage shall take
place in the territory of more than one State and that the place of departure or the place of destination, or
both these places, shall be situated on the territory of a Contracting State, irrespective of the place of
residence and the nationality of the Parties.
2.
