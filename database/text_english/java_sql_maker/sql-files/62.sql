4.
The limitations of damages referred to in this article shall apply to all the claims arising out of the
death or wounding of or any other bodily or mental injury to a passenger. Where there is more than one
claimant and the total of their claims exceeds the limit established, the claims shall be reduced
proportionately.
Section 2. Damage to Luggage
Article 14
1.
The carrier shall be liable for loss or damage resulting from the total or partial loss of luggage and
for damage thereto.
The carrier shall be responsible for luggage handed to him from the time when he takes charge of it
until the time either of its delivery or of its deposit in accordance with article 10, paragraph 3.
The carrier shall be responsible for other luggage while it is in the vehicle but, if in circumstances
other than of an accident, the luggage is stolen or cannot be found, the carrier shall be responsible only if
it had been placed in his care. Such other luggage shall be deemed to include personal effects carried or
worn by the passenger.
2.
The carrier shall be relieved of this liability if the loss or damage results from an inherent defect in
the luggage, from a special risk inherent in its perishable or dangerous nature or from circumstances
which a carrier using the diligence which the particular facts of the case called for could not have avoided
and the consequences of which he was unable to prevent.
3.
The carrier shall not be relieved of liability by reason of any physical or mental failing of the driver,
of any defect in or malfunctioning of the vehicle, or of any wrongful act or neglect on the part of the
person from whom he may have hired the vehicle or of the persons for whom the latter would have been
responsible, in accordance with the provisions of article4, if he had himself been the carrier.
Article 15
1.
Luggage not delivered within fourteen days from the date on which the passenger claims it shall be
deemed to be lost.
2.
If a piece of luggage which is deemed to be lost is recovered within one year following the date on
which the passenger claims it, the carrier shall take all reasonable steps to notify the passenger. Within
the thirty days following receipt of such notification the passenger may, against refund of any
compensation which may have been paid in respect of the loss but without prejudice to any claims to
compensation for delay, have the piece of luggage delivered to him either at the place of departure or at
the place appointed for delivery.
Article 16
1.
When under the provisions of this Convention a carrier is liable for compensation in respect of total
or partial loss of or in respect of damage to luggage, such compensation shall be claimable in an amount
equal to the amount of the loss or damage, but is shall not exceed 500 francs for each piece of luggage
nor 2,000 francs for each passenger. Compensation may also be claimed in respect of total or partial
loss of or in respect of damage to personal effects carried or worn by the passenger, but it shall not
exceed 1,000 francs for each passenger.
