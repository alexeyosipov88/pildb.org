/ [ANNEX] /
-----------
Provisions on customs matters relating to international multimodal transport of
goods
ARTICLE I
---------
For the purpose of this Convention:
"Customs transit procedure" means the customs procedure under which goods are
transported under customs control from one customs office to another.
"Customs office of destination" means any customs office at which a customs
transit operation is terminated.
"Import/export duties and taxes" means customs duties and all other duties,
taxes, fees or other charges which are collected on or in connection with the
import/export of goods, but not including fees and charge which are limited in
amount to the approximate cost of services rendered.
"Customs transit document" means a form containing the record of data entries
and information required for the customs transit operation.
ARTICLE II
----------
1. Subject to the provisions of the law, regulations and international
conventions in force in their territories, Contracting States shall grant
freedom of transit to goods in international multimodal transport.
2. Provided that the conditions laid down in the customs transit procedure used
for the transit operation are fulfilled to the satisfaction of the customs
authorities, goods in international multimodal transport:
(a) Shall not, as a general rule, be subject to customs examination during the
journey except to the extent deemed necessary to ensure compliance with rules
and regulations which the customs are responsible for enforcing. Flowing from
this, the customs authorities shall normally restrict themselves to the control
of customs seals and other security measures at points of entry and exit;
(b) Without prejudice to the application of law and regulations concerning
public or national security, public morality or public health, shall not be
subject to any customs formalities or requirements additional to those of the
customs transit regime used for the transit operation.
ARTICLE III
-----------
In order to facilitate the transit of the goods, each Contracting State shall:
(a) If it is the country of shipment, as far as practicable, take all measures
to ensure the completeness and accuracy of the information required for the
subsequent transit operations;
(b) If it is the country of destination;
(i) Take all necessary measures to ensure that goods in customs transit shall
be cleared, as a rule, at the customs office of destination of the goods;
(ii) Endeavour to carry out the clearance of goods at a place as near as is
possible to the place of final destination of the goods, provided that national
law and regulations do not require otherwise.
ARTICLE IV
----------
1. Provided that the conditions laid down in the customs transit procedure are
fulfilled to the satisfaction of the customs authorities, the goods in
international multimodal transport shall not be subject to the payment of
import/export duties and taxes or deposit in lieu thereof in transit countries.
2. The provisions of the preceding paragraph shall not preclude:
(a) The levy of fees and charges by virtue of national regulations on grounds
of public security or public health;
(b) The levy of fees and charges, which are limited in amount to the
approximate cost of services rendered, provided they are imposed under
conditions of equality.
ARTICLE V
---------
1. Where a financial guarantee for the customs transit operation is required,
it shall be furnished to the satisfaction of the customs authorities of the
transit country concerned in conformity with its national law and regulations
and international conventions.
2. With a view to facilitating customs transit, the system of customs guarantee
shall be simple, efficient, moderately priced and shall cover import/export
duties and taxes chargeable and, in countries where they are covered by
guarantees, any penalties due.
