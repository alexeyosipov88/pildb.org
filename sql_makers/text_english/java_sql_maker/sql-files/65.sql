VALUES(65, 'The States Parties to this Convention,
Recognising:
(a) That international multimodal transport is one means of facilitating the
orderly expansion of world trade;
(b) The need to stimulate the development of smooth, economic and efficient
multimodal transport services adequate to the requirements of the trade
concerned;
(c) The desirability of ensuring the orderly development of international
multimodal transport in the interest of all countries and the need to consider
the special problems of transit countries;
(d) The desirability of determining certain rules relating to the carriage of
goods by international multimodal transport contracts, including equitable
provisions concerning the liability of multimodal transport operators;
(e) The need that this Convention should not affect the application of any
international convention or national law relating to the regulation and control
of transport operations;
(f) The right of each State to regulate and control at the national level
multimodal transport operators and operations;
(g) The need to have regard to the special interest and problems of developing
countries, for example, as regards introduction of new technologies,
participation in multimodal services of their national carriers and operators,
cost efficiency thereof and maximum use of local labour and insurance;
(h) The need to ensure a balance of interests between suppliers and users of
multimodal transport services;
(i) The need to facilitate customs procedures with due consideration to the
problems of transit countries;
Agreeing to the following basic principles:
(a) That a fair balance of interests between developed and developing countries
should be established and an equitable distribution of activities between these
groups of countries should be attained in international multimodal transport;
(b) That consultation should take place on terms and conditions of service,
both before and after the introduction of any new technology in the multimodal
transport of goods, between the multimodal transport operator, shippers,
shippers'' organisations and appropriate national authorities;
(c) The freedom for shippers to choose between multimodal and segmented
transport services;
(d) That the liability of the multimodal transport operator under this
Convention should be based on the principle of presumed fault or neglect;
Have decided to conclude a Convention for this purpose and have thereto agreed
as follows:
PART I - GENERAL PROVISIONS
===========================
ARTICLE 1 - DEFINITIONS
-----------------------
For the purposes of this Convention:
1. "International multimodal transport" means the carriage of goods by at least
two different modes of transport on the basis of a multimodal transport
contract from a place in one country at which the goods are taken in charge by
the multimodal transport operator to a place designated for delivery situated
in a different country. The operations of pick-up and delivery of goods carried
out in the performance of a unimodal transport contract, as defined in such
contract, shall not be considered as international multimodal transport.
2. "Multimodal transport operator" means any person who on his own behalf or
through another person acting on his behalf concludes a multimodal transport
contract and who acts as a principal, not as an agent or on behalf of the
consignor or of the carriers participating in the multimodal transport
operations, and who assumes responsibility for the performance of the contract.
3. "Multimodal transport contract" means a contract whereby a multimodal
transport operator undertakes, against payment of freight, to perform or to
procure the performance of international multimodal transport. 4. "Multimodal
transport document" means a document which evidences a multimodal transport
contract, the taking in charge of the goods by the multimodal transport
operator, and an undertaking by him to deliver the goods in accordance with the
terms of that contract.
5. "Consignor" means any person by whom or in whose name or on whose behalf a
multimodal transport contract has been concluded with the multimodal transport
operator, or any person by whom or in whose name or on whose behalf the goods
are actually delivered to the multimodal transport operator in relation to the
multimodal transport contract.
6. "Consignee" means the person entitled to take delivery of the goods.
7. "Goods" includes any container, pallet or similar article of transport or
packaging, if supplied by the consignor.
8. "International convention" means an international agreement concluded among
States in written form and governed by international law.
9. "Mandatory national law" means any statutory law concerning carriage of
goods the provisions of which cannot be departed from by contractual
stipulation to the detriment of the consignor.
10. "Writing" means, inter alia, telegram or telex.
ARTICLE 2 - SCOPE OF APPLICATION
--------------------------------
The provisions of this Convention shall apply to all contracts of multimodal
transport between places in two States, if:
(a) The place for the taking in charge of the goods by the multimodal transport
operator as provided for in the multimodal transport contract is located in a
Contracting State, or
(b) The place for delivery of the goods by the multimodal transport operator as
provided for in the multimodal transport contract is located in a Contracting
State.
ARTICLE 3 - MANDATORY APPLICATION
---------------------------------
1. When a multimodal transport contract has been concluded which according to
article 2 shall be governed by this Convention, the provisions of this
Convention shall be mandatorily applicable to such contract.
2. Nothing in this Convention shall affect the right of the consignor to choose
between multimodal transport and segmented transport.
ARTICLE 4 - REGULATION AND CONTROL OF MULTIMODAL TRANSPORT
----------------------------------------------------------
1. This Convention shall not affect, or be incompatible with, the application
of any international convention or national law relating to the regulation and
control of transport operations.
2. This Convention shall not affect the right of each State to regulate and
control at the national level multimodal transport operations and multimodal
transport operators, including the right to take measures relating to
consultations, especially before the introduction of new technologies and
services, between multimodal transport operators, shippers, shippers''
organisations and appropriate national authorities on terms and conditions of
service; licensing of multimodal transport operators; participation in
transport; and all other steps in the national economic and commercial
interest.
3. The multimodal transport operator shall comply with the applicable law of
the country in which he operates and with the provisions of this Convention.
PART II - DOCUMENTATION
=======================
ARTICLE 5 - ISSUE OF MULTIMODAL TRANSPORT DOCUMENT
--------------------------------------------------
1. When the goods are taken in charge by the multimodal transport operator, he
shall issue a multimodal transport document which, at the option of the
consignor, shall be in either negotiable or non-negotiable form.
2. The multimodal transport document shall be signed by the multimodal
transport operator or by a person having authority from him.
3. The signature on the multimodal transport document may be in handwriting,
printed in facsimile, perforated, stamped, in symbols, or made by any other
mechanical or electronic means, if no inconsistent with the law of the country
where the multimodal transport document is issued.
4. If the consignor so agrees, a non-negotiable multimodal transport document
may be issued by making use of any mechanical or other means preserving a
record of the particulars stated in article 8 to be contained in the multimodal
transport document. In such a case the multimodal transport operator, after
having taken the goods in charge, shall deliver to the consignor a readable
document containing all the particulars so recorded, and such document shall
for the purposes of the provisions of this Convention be deemed to be a
multimodal transport document.
ARTICLE 6 - NEGOTIABLE MULTIMODAL TRANSPORT DOCUMENT
----------------------------------------------------
1. Where a multimodal transport document is issued in negotiable form:
(a) It shall be made out to order or to bearer;
(b) If made out to order it shall be transferable by endorsement;
(c) If made out to bearer it shall be transferable without endorsement;
(d) If issued in a set of more than one original it shall indicate the number
of originals in the set;
(e) If any copies are issued each copy shall be marked "non-negotiable copy"
2. Delivery of the goods may be demanded from the multimodal transport operator
or a person acting on his behalf only against surrender of the negotiable
multimodal transport document duly endorsed where necessary.
3. The multimodal transport operator shall be discharged from his obligation to
deliver the goods if, where a negotiable multimodal transport document has been
issued in a set of more than one original, he or a person acting on his behalf
has in good faith delivered the goods against surrender of one of such
originals.
ARTICLE 7- NON-NEGOTIABLE MULTIMODAL TRANSPORT
----------------------------------------------
1. Where a multimodal transport document is issued in non-negotiable form it
shall indicate a named consignee.
2. The multimodal transport operator shall be discharged from his obligation to
deliver the goods if he makes delivery thereof to the consignee named in such
non-negotiable multimodal transport document or to such other person as he may
be duly instructed, as a rule, in writing.
ARTICLE 8 - CONTENTS OF THE MULTIMODAL TRANSPORT DOCUMENT
---------------------------------------------------------
1. The multimodal transport document shall contain the following particulars:
(a) The general nature of the goods, the leading marks necessary for
identification of the goods, an express statement, if applicable, as to the
dangerous character of the goods, the number of packages or pieces, and the
gross weight of the goods or their quantity otherwise expressed, all such
particulars as furnished by the consignor;
(b) The apparent condition of the goods;
(c) The name and principal place of business of the multimodal transport
operator;
(d) The name of the consignor;
(e) The consignee, if named by the consignor;
(f) The place and date of taking in charge of the goods by the multimodal
transport operator;
(g) The place of delivery of the goods;
(h) The date or the period of delivery of the goods at the place of delivery,
if expressly agreed upon between the parties;
(i) A statement indicating whether the multimodal transport document is
negotiable or non-negotiable;
(j) The place and date of issue of the multimodal transport document;
(k) The signature of the multimodal transport operator or of a person having
authority from him;
(l) The freight for each mode of transport, if expressly agreed between the
parties, or the freight including its currency, to the extent payable by the
consignee or other indication that freight is payable by him;
(m) The intended journey route, modes of transport and places of transhipment,
if known at the time of issuance of the multimodal transport document;
(n) The statement referred to in paragraph 3 of article 28;
(o) Any other particulars which the parties may agree to insert in the
multimodal transport document, if not inconsistent with the law of the country
where the multimodal transport document is issued.
2. The absence from the multimodal transport document of one or more of the
particulars referred to in paragraph 1 of this article shall not affect the
legal character of the document as a multimodal transport document provided
that it nevertheless meets the requirements set out in paragraph 4 of article
1.
ARTICLE 9 - RESERVATIONS IN THE MULTIMODAL TRANSPORT DOCUMENT
-------------------------------------------------------------
1. If the multimodal transport document contains particulars concerning the
general nature, leading marks, number of packages or pieces, weight or quantity
of the goods which the multimodal transport operator or a person acting on his
behalf knows, or has reasonable grounds to suspect, do not accurately represent
the goods actually taken in charge, or if he has no reasonable means of
checking such particulars, the multimodal transport operator or a person acting
on his behalf shall insert in the multimodal transport document a reservation
specifying these inaccuracies, grounds of suspicion or the absence of
reasonable means of checking.
2. If the multimodal transport operator or a person acting on his behalf fails
to note on the multimodal transport document the apparent condition of the
goods, he is deemed to have noted on the multimodal transport document that the
goods were in apparent good condition.
ARTICLE 10 - EVIDENTIARY EFFECT OF THE MULTIMODAL TRANSPORT DOCUMENT
--------------------------------------------------------------------
Except for particulars in respect of which and to the extent to which a
reservation permitted under article 9 has been entered:
(a) The multimodal transport document shall be prima facie evidence of the
taking in charge by the multimodal transport operator of the goods as described
therein; and
(b) Proof to the contrary by the multimodal transport operator shall not be
admissible if the multimodal transport document is issued in negotiable form
and has been transferred to a third party, including a consignee, who has acted
in good faith in reliance on the description of the goods therein.
ARTICLE 11 - LIABILITY FOR INTENTIONAL MISSTATEMENTS OR OMISSIONS
-----------------------------------------------------------------
When the multimodal transport operator, with intent to defraud, gives in the
multimodal transport document false information concerning the goods or omits
any information required to be included under paragraph 1 (a) or (b) of article
8 or under article 9, he shall be liable, without the benefit of the limitation
of liability provided for in this Convention, for any loss, damage or expenses
incurred by a third party, including a consignee, who acted in reliance on the
description of the goods in the multimodal transport document issued.
ARTICLE 12 - GUARANTEE BY THE CONSIGNOR
---------------------------------------
1. The consignor shall be deemed to have guaranteed to the multimodal transport
operator the accuracy, at the time the goods were taken in charge by the
multimodal transport operator, of particulars relating to the general nature of
the goods, their marks, number, weight and quantity and, if applicable, to the
dangerous character of the goods, as furnished by him for insertion in the
multimodal transport document.
2. The consignor shall indemnify the multimodal transport operator against loss
resulting from inaccuracies in or inadequacies of the particulars referred to
in paragraph 1 of this article. The consignor shall remain liable even if the
multimodal transport document has been transferred to him. The right of the
multimodal transport operator to such indemnity shall in no way limit his
liability under the multimodal transport contract to any person other than the
consignor.
ARTICLE 13 - OTHER DOCUMENTS
----------------------------
The issue of the multimodal transport document does not preclude the issue, if
necessary, of other documents relating to transport or other services involved
in international multimodal transport, in accordance with applicable
international conventions or national law. However, the issue of such other
documents shall not affect the legal character of the multimodal transport
document.
PART III - LIABILITY OF THE MULTIMODAL TRANSPORT OPERATOR
=========================================================
ARTICLE 14- PERIOD OF RESPONSIBILITY
------------------------------------
1. The responsibility of the multimodal transport operator for the goods under
this Convention covers the period from the time he takes the goods in his
charge to the time of their delivery.
2. For the purpose of this article, the multimodal transport operator is deemed
to be in charge of the goods:
(a) From the time he has taken over the goods from:
(i) The consignor or a person acting on his behalf; or
(ii) An authority or other third party to whom, pursuant to law or regulations
applicable at the place of taking in charge, the goods must be handed over for
transport;
(b) Until the time he has delivered the goods:
(i) By handing over the goods to the consignee; or
(ii) In cases where the consignee does not receive the goods from the
multimodal transport operator, by placing them at the disposal of the consignee
in accordance with the multimodal transport contract or with the law or with
the usage of the particular trade applicable at the place of delivery; or
(iii) By handing over the goods to an authority or other third party to whom,
pursuant to law or regulations applicable at the place of delivery, the goods
must be handed over.
3. In paragraphs 1 and 2 of this article, reference to the multimodal transport
operator shall include his servants or agents or any other person of whose
services he makes use for the performance of the multimodal transport contract,
and reference to the consignor or consignee shall include their servants or
agents.
ARTICLE 15 - THE LIABILITY OF THE MULTIMODAL TRANSPORT OPERATOR FOR HIS
SERVANTS, AGENTS AND OTHER PERSONS
------------------------------------------------------------------------------
Subject to article 21, the multimodal transport operator shall be liable for
the acts and omissions of his servants or agents, when any such servant or
agent is acting within the scope of his employment, or of any other person of
whose services he makes use for the performance of the multimodal transport
contract, when such person is acting in the performance of the contract, as if
such acts and omissions were his own.
ARTICLE 16- BASIS OF LIABILITY
------------------------------
1. The multimodal transport operator shall be liable for loss resulting from
loss or damage to the goods, as well as from delay in delivery, if the
occurrence which caused the loss, damage or delay in delivery took place while
the goods were in his charge as defined in article 14, unless the multimodal
transport operator proves that he, his servants or agents or any other person
referred to in article 15 took all measures that could reasonably be required
to avoid the occurrence and its consequences.
2. Delay in delivery occurs when the goods have not been delivered within the
time expressly agreed upon or, in the absence of such agreement, within the
time which it would be reasonable to require of a diligent multimodal transport
operator, having regard to the circumstances of the case.
3. If the goods have not been delivered within 90 consecutive days following
the date of delivery determined according to paragraph 2 of this article, the
claimant may treat the goods as lost.
ARTICLE 17- CONCURRENT CAUSES
-----------------------------
Where fault or neglect on the part of the multimodal transport operator, his
servants or agents or any other person referred to in article 15 combines with
another cause to produce loss, damage or delay in delivery, the multimodal
transport operator shall be liable only to the extent that the loss, damage or
delay in delivery is attributable to such fault or neglect, provided that the
multimodal transport operator proves the part of the loss, damage or delay in
delivery not attributable thereto.
ARTICLE 18- LIMITATION OF LIABILITY
-----------------------------------
1. When the multimodal transport operator is liable for loss resulting from
loss of or damage to the goods according to article 16, his liability shall be
limited to an amount not exceeding 920 units of account per package of other
shipping unit or 2.75 units of account per kilogram of gross weight of the
goods lost or damaged, whichever is the higher.
2. For the purpose of calculating which amount is the higher in accordance with
paragraph 1 of this article, the following rules apply:
(a) Where a container, pallet or similar article of transport is used to
consolidate goods, the packages or other shipping units enumerated in the
multimodal transport document as packed in such article of transport are deemed
packages or shipping units. Except as aforesaid, the goods in such article of
transport are deemed one shipping unit.
(b) In cases where the article of transport itself has been lost or damaged,
that article of transport, if no owned or otherwise supplied by the multimodal
transport operator, is considered one separate shipping unit.
3. Notwithstanding the provisions of paragraphs 1 and 2 of this article, if the
international multimodal transport does not, according to the contract, include
carriage of goods by sea or by inland waterways, the liability of the
multimodal transport operator shall be limited to an amount not exceeding 8.33
units of account per kilogram of gross weight of the goods lost or damaged.
4. The liability of the multimodal transport operator for loss resulting from
delay in delivery according to the provisions of article 16 shall be limited to
an amount equivalent to two and a half times the freight payable for the goods
delayed, but not exceeding the total freight payable under the multimodal
transport contract.
5. The aggregate liability of the multimodal transport operator, under
paragraphs 1 and 4 or paragraphs 3 and 4 of this article, shall not exceed the
limit of liability for total loss of the goods as determined by paragraph 1 or
3 of this article.
6. By agreement between the multimodal transport operator and the consignor,
limits of liability exceeding those provided for in paragraphs 1, 3 and 4 of
this article may be fixed in the multimodal transport document.
7. "Unit of account" means the unit of account mentioned in article 31.
ARTICLE 19- LOCALISED DAMAGE
----------------------------
When the loss of or damage to the goods occurred during one particular of the
multimodal transport, in respect of which an applicable international
convention or mandatory national law provides a higher limit of liability than
the limit that would follow from application of paragraphs 1 to 3 of article
18, then the limit of the multimodal transport operator''s liability for such
loss or damage shall be determined by reference to the provisions of such
convention or mandatory national law.
ARTICLE 20 - NON-CONTRACTUAL LIABILITY
--------------------------------------
1. The defences and limits of liability provided for in this Convention shall
apply in any action against the multimodal transport operator in respect of
loss resulting from loss of or damage to the goods, as well as from delay in
delivery, whether the action be founded in contract, in tort or otherwise.
2. If an action in respect of loss resulting from loss of or damage to the
goods or from delay in delivery is brought against the servant or agent of the
multimodal transport operator, if such servant or agent proves that he acted
within the scope of his employment, or against any other person of whose
services he makes use for the performance of the multimodal transport contract,
if such other person proves that he acted within the performance of the
contract, the servant or agent of such other person shall be entitled to avail
himself of the defences and limits of liability which the multimodal transport
operator is entitled to invoke under this Convention.
3. Except as provided in article 21, the aggregate of the amounts recoverable
from the multimodal transport operator and from a servant or agent or any other
person of whose services he makes use for the performance of the multimodal
transport contract shall not exceed the limits of liability provided for in
this Convention.
ARTICLE 21 - LOSS OF THE RIGHT TO LIMIT LIABILITY
-------------------------------------------------
1. The multimodal transport operator is not entitled to the benefit of the
limitation of liability provided for in this Convention if it is proved that
the loss, damage or delay in delivery resulted from an act or omission of the
multimodal transport operator done with the intent to cause such loss, damage
or delay or recklessly and with knowledge that such loss, damage or delay would
probably result.
2. Notwithstanding paragraph 2 of article 20, a servant or agent of the
multimodal transport operator or other person of whose services he makes use
for the performance of the multimodal transport contract is not entitled to the
benefit of the limitation of liability provided for in this Convention if it is
proved that the loss, damage or delay in delivery resulted from an act or
omission of such servant, agent or other person, done with the intent to cause
such loss, damage or delay or recklessly and with knowledge that such loss,
damage or delay would probably result.
PART IV - LIABILITY OF THE CONSIGNOR
====================================
ARTICLE 22 - GENERAL RULE
-------------------------
The consignor shall be liable for loss sustained by the multimodal transport
operator if such loss is caused by the fault or neglect of the consignor, or
his servants or agents when such servants or agents are acting within the scope
of their employment. Any servant or agent of the consignor shall be liable for
such loss if the loss is caused by fault or neglect on his part.
ARTICLE 23 - SPECIAL RULES ON DANGEROUS GOODS
---------------------------------------------
1. The consignor shall mark or label in a suitable manner dangerous goods as
dangerous.
2. Where the consignor hands over dangerous goods to the multimodal transport
operator or any person acting on his behalf, the consignor shall inform him of
the dangerous character of the goods and, if necessary, the precautions to be
taken. If the consignor fails to do so and the multimodal transport operator
does not otherwise have knowledge of their dangerous character:
(a) The consignor shall be liable to the multimodal transport operator for all
loss resulting from the shipment of such goods; and
(b) The goods may at any time be unloaded, destroyed or rendered innocuous, as
the circumstances may require, without payment of compensation.
3. The provisions of paragraph 2 of this article may not be invoked by any
person if during the multimodal transport he has taken the goods in his charge
with knowledge of their dangerous character.
4. If, in cases where the provisions of paragraph 2 (b) of this article do not
apply or may not be invoked, dangerous goods become an actual danger to life or
property, they may be unloaded, destroyed or rendered innocuous, as the
circumstances may require, without payment of compensation except where there
is an obligation to contribute in general average or where the multimodal
transport operator is liable in accordance with the provisions of article 16.
PART V - CLAIMS AND ACTIONS
===========================
ARTICLE 24 - NOTICE OF LOSS, DAMAGE OR DELAY
--------------------------------------------
1. Unless notice of loss or damage, specifying the general nature of such loss
or damage, is given in writing by the consignee to the multimodal transport
operator not later than the working day after the day when the goods were
handed over to the consignee, such handing over is prima facie evidence of the
delivery by the multimodal transport operator of the goods as described in the
multimodal transport document.
2. Where the loss or damage is not apparent, the provisions of paragraph 1 of
this article apply correspondingly if notice in writing is not given within six
consecutive days after the day when the goods were handed over to the
consignee.
3. If the state of the goods at the time they were handed over to the consignee
has been the subject of a joint survey or inspection by the parties or their
authorised representatives at the place of delivery, notice in writing need not
be given of loss or damage ascertained during such survey or inspection.
4. In the case of any actual or apprehended loss or damage the multimodal
transport operator and the consignee shall give all reasonable facilities to
each other for inspecting and tallying the goods.
5. No compensation shall be payable for loss resulting from delay in delivery
unless notice has been given in writing to the multimodal transport operator
within 60 consecutive days after the day when the goods were delivered by
handing over to the consignee or when the consignee has been notified that the
goods have been delivered in accordance with paragraph 2 (b) (ii) or (iii) of
article 14.
6. Unless notice of loss or damage, specifying the general nature of the loss
or damage, is given in writing by the multimodal transport operator to the
consignor not later than 90 consecutive days after the occurrence of such loss
or damage or after the delivery of the goods in accordance with paragraph 2 (b)
of article 14, whichever is later, the failure to give such notice is prima
facie evidence that the multimodal transport operator has sustained no loss or
damage due to the fault or neglect of the consignor, his servants or agents.
7. If any of the notice periods provided for in paragraphs 2,5 and 6 of this
article terminates on a day which is not a working day at the place of
delivery, such period shall be extended until the next working day.
8. For the purpose of this article, notice given to a person acting on the
multimodal transport operator''s behalf, including any person of whose services
he makes use at the place of delivery, or to a person acting on the consignor''s
behalf, shall be deemed to have been given to the multimodal transport
operator, or to the consignor, respectively.
ARTICLE 25 - LIMITATION OF ACTIONS
----------------------------------
1. Any action relating to international multimodal transport under this
Convention shall be time-barred if judicial or arbitral proceedings have not
been instituted within a period of two years. However, if notification in
writing, stating the nature and main particulars of the claim, has not been
given within six months after the day when the goods were delivered or, where
the goods have not been delivered, after the day on which they should have been
delivered, the action shall be time-barred at the expiry of this period.
2. The limitation period commences on the day after the day on which the
multimodal transport operator has delivered the goods or part thereof or, where
the goods have not been delivered, on the day after the last day on which the
goods should have been delivered.
3. The person against whom a claim is made may at any time during the running
of the limitation period extend that period by a declaration in writing to the
claimant. This period may be further extended by another declaration or
declarations.
4. Provided that the provisions of another applicable international convention
are not to the contrary, a recourse action for indemnity by a person held
liable under this Convention may be instituted even after the expiration of the
limitation period provided for in the preceding paragraphs if instituted within
the time allowed by the law of the State where proceedings are instituted;
however, the time allowed shall not be less than 90 days commencing from the
day when the person instituting such action for indemnity has settled the claim
or has been served with process in the action against himself.
ARTICLE 26 - JURISDICTION
-------------------------
1. In judicial proceedings relating to international multimodal transport under
this Convention, the plaintiff, at his option, may institute an action in a
court which, according to the law of the State where the court is situated, is
competent and within the jurisdiction of which is situated one of the following
places:
(a) The principal place of business or, in the absence thereof, the habitual
residence of the defendant; or
(b) The place where the multimodal transport contract was made, provided that
the defendant has there a place of business, branch or agency through which the
contract was made; or
(c) The place of taking the goods in charge for international multimodal
transport or the place of delivery; or
(d) Any other place designated for that purpose in the multimodal transport
contract and evidenced in the multimodal transport document.
2. No judicial proceedings relating to international multimodal transport under
this Convention may be instituted in a place not specified in paragraph 1 of
this article. The provisions of this article do not constitute an obstacle to
the jurisdiction of the Contracting States for provisional or protective
measures.
3. Notwithstanding the preceding provisions of this article, an agreement made
by the parties after a claim has arisen, which designates the place where the
plaintiff may institute an action, shall be effective.
4. (a) Where an action has been instituted in accordance with the provisions of
this article or where judgement in such an action has been delivered, no new
action shall be instituted between the same parties on the same grounds unless
the judgement in the first action is not enforceable in the country in which
the new proceedings are instituted;
(b) For the purposes of this article neither the institution of measures to
obtain enforcement of a judgement nor the removal of an action to a different
court within the same country shall be considered as the starting of a new
action
ARTICLE 27- ARBITRATION
-----------------------
1. Subject to the provisions of this article, parties may provide by agreement
evidenced in writing that any dispute that may arise relating to international
multimodal transport under this Convention shall be referred to arbitration.
2. The arbitration proceedings shall, at the option of the claimant, be
instituted at one of the following places:
(a) A place in a State within whose territory is situated:
(i) The principal place of business of the defendant or, in the absence
thereof, the habitual residence of the defendant; or
(ii) The place where the multimodal transport contract was made, provided that
the defendant has there a place of business, branch or agency through which the
contract was made; or (iii) The place of taking the goods in charge for
international multimodal transport or the place of delivery; or
(b) Any other place designated for that purpose in the arbitration clause or
agreement.
3. The arbitrator or arbitration tribunal shall apply the provisions of this
Convention.
4. The provisions of paragraphs 2 and 3 of this article shall be deemed to be
part of every arbitration clause or agreement and any term of such clause or
agreement which is inconsistent therewith shall be null and void.
5. Nothing in this article shall affect the validity of an agreement on
arbitration made by the parties after the claim relating to the international
multimodal transport has arisen.
PART VI - SUPPLEMENTARY PROVISIONS
==================================
ARTICLE 28 - CONTRACTUAL STIPULATIONS
-------------------------------------
1. Any stipulation in a multimodal transport contract or multimodal transport
document shall be null and void to the extent that it derogates, directly or
indirectly, from the provisions of this Convention. The nullity of such a
stipulation shall not affect the validity of other provisions of the contract
or document of which it forms a part. A clause assigning benefit of insurance
of the goods in favour of the multimodal transport operator or any similar
clause shall be null and void.
2. Notwithstanding the provisions of paragraph 1 of this article, the
multimodal transport operator may, with the agreement of the consignor,
increase his responsibilities and obligations under this Convention.
3. The multimodal transport document shall contain a statement that the
international multimodal transport is subject to the provisions of this
Convention which nullify any stipulation derogating therefrom to the detriment
of the consignor or the consignee.
4. Where the claimant in respect of the goods has incurred loss as a result of
a stipulation which is null and void by virtue of the present article, or as a
result of the omission of the statement referred to in paragraph 3 of this
article, the multimodal transport operator must pay compensation to the extent
required in order to give the claimant compensation in accordance with the
provisions of this Convention for any loss of or damage to the goods as well as
for delay in delivery. The multimodal transport operator must, in addition, pay
compensation for costs incurred by the claimant for the purpose of exercising
his right, provided that costs incurred in the action where the foregoing
provision is invoked are to be determined in accordance with the law of the
State where proceedings are instituted.
ARTICLE 29 - GENERAL AVERAGE
----------------------------
1. Nothing in this Convention shall prevent the application of provisions in
the multimodal transport contract or national law regarding the adjustment of
general average, if and to the extent applicable.
2. With the exception of article 25, the provisions of this Convention relating
to the liability of the multimodal transport operator for loss of or damage to
the goods shall also determine whether the consignee may refuse contribution in
general average and the liability of the multimodal transport operator to
indemnify the consignee in respect of any such contribution made or any salvage
paid.
ARTICLE 30 - OTHER CONVENTIONS
------------------------------
1. This Convention does not modify the rights or duties provided for in the
Brussels International Convention for the unification of certain rules relating
to the limitation of the liability of owners of sea-going vessels of 25 August
1924; in the Brussels International Convention relating to the limitation of
the liability of owners of sea-going ships of 10 October 1957; in the London
Convention on limitation of liability for maritime claims of 19 November 1976;
and in the Geneva Convention relating to the limitation of the liability of
owners of inland navigation vessels (CLN) of 1 March 1973, including amendments
to these Conventions, or national law relating to the limitation of liability
of owners of sea-going ships and inland navigation vessels.
2. The provisions of articles 26 and 27 of this Convention do not prevent the
application of the mandatory provisions of any other international convention
relating to matters dealt with in the said articles, provided that the dispute
arises exclusively between parties having their principal place of business in
States parties to such other convention. However, this paragraph does not
affect the application of paragraph 3 of article 27 of this Convention.
3. No liability shall arise under the provisions of this Convention for damage
caused by nuclear incident if the operator of a nuclear installation is liable
for such damage:
(a) Under either the Paris Convention of 29 July 1960 on Third Party Liability
in the Field of Nuclear Energy as amended by the Additional Protocol of 28
January 1964 or the Vienna Convention of 21 May 1963 on Civil Liability for
Nuclear Damage, or amendments thereto; or
(b) By virtue of national law governing the liability for such damage, provided
that such law is in all respects as favourable to persons who may suffer damage
as either the Paris or Vienna Conventions.
4. Carriage of goods such as carriage of goods in accordance with the Geneva
Convention of 19 May 1956 on the Contract for the International Carriage of
Goods by Road in article 2, or the Berne Convention of 7 February 1970
concerning the Carriage of Goods by Rail, article 2, shall not for States
Parties to Conventions governing such carriage be considered as international
multimodal transport within the meaning of article 1, paragraph 1, of this
Convention, in so far as such States are bound to apply the provisions of such
Conventions to such carriage of goods.
ARTICLE 31 - UNIT OF ACCOUNT OF MONETARY UNIT AND CONVERSION
------------------------------------------------------------
1. The unit of account referred to in article 18 of this Convention is the
Special Drawing Right as defined by the International Monetary Fund. The
amounts referred to in article 18 shall be converted into the national currency
of a State according to the value of such currency on the date of the judgement
or award or the date agreed upon by the parties. The value of a national
currency, in terms of the Special Drawing Right, of a Contracting State which
is a member of the International Monetary Fund, shall be calculated in
accordance with the method of valuation applied by the International Monetary
Fund, in effect on the date in question, for its operations and transactions.
The value of a national currency in terms of the Special Drawing right of a
Contracting State which is not a member of the International Monetary Fund
shall be calculated in a manner determined by that State.
2. Nevertheless, a State which is not a member of the International Monetary
Fund and whose law does not permit the application of the provisions of
paragraph 1 of this article may, at the time of signature, ratification,
acceptance, approval or accession, or at any time thereafter, declare that the
limits of liability provided for in this Convention to be applied in its
territory shall be fixed as follows: with regard to the limits provided for in
paragraph 1 of article 18, to 13,750 monetary units per package or other
shipping unit or 41.25 monetary units per kilogram of gross weight of the
goods, and with regard to the limit provided for in paragraph 3 of article 18,
to 124 monetary units.
3. The monetary unit referred to in paragraph 2 of this article corresponds to
sixty-five and a half milligrams of gold of millesimal fineness nine hundred.
The conversion of the amount referred to in paragraph 2 of this article into
national currency shall be made according to the law of the State concerned.
4. The calculation mentioned in the last sentence of paragraph 1 of this
article and the conversion referred to in paragraph 3 of this article shall be
made in such a manner as to express in the national currency of the Contracting
State as far as possible the same real value for the amounts in article 18 as
is expressed there in units of account.
5. Contracting States shall communicate to the depositary the manner of
calculation pursuant to the last sentence of paragraph 1 of this article, or
the result of the conversion pursuant to paragraph 3 of this article, as the
case may be, at the time of signature or when depositing their instruments of
ratification, acceptance, approval or accession, or when availing themselves of
the option provided for in paragraph 2 of this article and whenever there is a
change in the manner of such calculation or in the result of such conversion.
PART VII - CUSTOMS MATTERS
==========================
ARTICLE 32 - CUSTOMS TRANSIT
----------------------------
1. Contracting States shall authorise the use of the procedure of customs
transit for international multimodal transport.
2. Subject to provisions of national law or regulations and intergovernmental
agreements, the customs transit of goods in international multimodal transport
shall be in accordance with the rules and principles contained in articles I to
VI of the annex to this Convention.
3. When introducing laws or regulations in respect of customs transit
procedures relating to multimodal transport of goods, Contracting States should
take into consideration articles I to VI of the annex to this Convention.
PART VIII - FINAL CLAUSES
=========================
ARTICLE 33 - DEPOSITARY
-----------------------
The Secretary-General of the United Nations is hereby designated as the
depositary of this Convention.
ARTICLE 34 - SIGNATURE, RATIFICATION, ACCEPTANCE, APPROVAL AND ACCESSION
------------------------------------------------------------------------
1. All States are entitled to become Parties to this Convention by:
(a) Signature not subject to ratification, acceptance or approval; or
(b) Signature subject to and followed by ratification, acceptance or approval;
or
(c) Accession.
2. This Convention shall be open for signature as from 1 September 1980 until
and including 31 August 1981 at the Headquarters of the United Nations in New
York.
3. After 31 August 1981, this Convention shall be open for accession by all
States which are not signatory States.
4. Instruments of ratification, acceptance, approval and accession are to be
deposited with the depositary.
5. Organisations for regional economic integration, constituted by sovereign
States members of UNCTAD, and which have competence to negotiate, conclude and
apply international agreements in specific fields covered by this Convention,
shall be similarly entitled to become Parties to this Convention in accordance
with the provisions of paragraphs 1 to 4 of this article, thereby assuming in
relation to other Parties to this Convention the rights and duties under this
Convention in the specific fields referred to above.
ARTICLE 35 - RESERVATIONS
-------------------------
No reservation may be made to this Convention.
ARTICLE 36 - ENTRY INTO FORCE
-----------------------------
1. This Convention shall enter into force 12 months after the Governments of 30
States have either signed it not subject to ratification, acceptance or
approval or have deposited instruments of ratification, acceptance, approval or
accession with the depositary.
2. For each State which ratifies, accepts, approves or accedes to this
Convention after the requirements for entry into force given in paragraph 1 of
this article have been met, the Convention shall enter into force 12 months
after the deposit by such State of the appropriate instrument.
ARTICLE 37 - DATE OF APPLICATION
--------------------------------
Each Contracting State shall apply the provisions of this Convention to
multimodal transport contracts concluded on or after the date of entry into
force of this Convention in respect of that State.
ARTICLE 38 - RIGHTS AND OBLIGATIONS UNDER EXISTING CONVENTIONS
--------------------------------------------------------------
If, according to articles 26 or 27, judicial or arbitral proceedings are
brought in a Contracting State in a case relating to international multimodal
transport subject to this Convention which takes place between two States of
which only one is a Contracting State, and if both these States are at the time
of entry into force of this Convention equally bound by another international
convention, the court or arbitral tribunal may, in accordance with the
obligations under such convention, give effect to the provisions thereof.
ARTICLE 39 - REVISION AND AMENDMENTS
------------------------------------
1. At the request of not less than one third of the Contracting States, the
Secretary-General of the United Nations shall, after the entry into force of
this Convention, convene a conference of the Contracting States for revising or
amending it. The Secretary-General of the United Nations shall circulate to all
Contracting States the texts of any proposals for amendments at least three
months before the opening date of the conference.
2. Any decision by the revision conference, including amendments, shall be
taken by a two thirds majority of the States present and voting. Amendments
adopted by the conference shall be communicated by the depositary to all the
contracting States for acceptance and to all the States signatories of the
Convention for information.
3. Subject to paragraph 4 below, any amendment adopted by the conference shall
enter into force only for those Contracting States which have accepted it, on
the first day of the month following one year after its acceptance by two
thirds of the Contracting States. For any State accepting an amendment after it
has been accepted by two thirds of the Contracting States, the amendment shall
enter into force on the first day of the month following one year after its
acceptance by that State.
4. Any amendment adopted by the conference altering the amounts specified in
article 18 and paragraph 2 of article 31 or substituting either or both the
units defined in paragraphs 1 and 3 of article 31 by other units shall enter
into force on the first day of the month following one year after its
acceptance by two thirds of the Contracting States. Contracting States which
have accepted the altered amounts or the substituted units shall apply them in
their relationship with all Contracting States.
5. Acceptance of amendments shall be effected by the deposit of a formal
instrument to that effect with the depositary.
6. Any instrument of ratification, acceptance, approval or accession deposited
after the entry into force of any amendment adopted by the conference shall be
deemed to apply to the Convention as amended.
ARTICLE 40 - DENUNCIATION
-------------------------
1. Each Contracting State may denounce this Convention at any time after the
expiration o a period of two years from the date on which this Convention has
entered into force by means of a notification in writing addressed to the
depositary.
2. Such denunciation shall take effect on the first day of the month following
the expiration of one year after the notification is received by the
depositary. Where a longer period is specified in the notification, the
denunciation shall take effect upon the expiration of such longer period after
the notification is received by the depositary.
