Article 17
1.
The period of limitation for actions arising out of the death of, or personal
injury or any other bodily or mental harm to, a passenger shall be three years.
The period of limitation shall run from the date on which the loss or damage
has come to the knowledge of the person suffering it. The period of limitation shall
not, however, exceed five years from the date of the accident.
2. The period of limitation for actions arising out of carriage under this
Convention, other than the actions referred to in paragraph 1,. shall in all cases be
one year.
The period of limitation shall run from the date on which the vessel arrived at
the place of destination of the passenger or, in the case of non-arrival, from the
date on which the vessel ought to have arrived.
3. A written claim shall suspend the period of limitation until such date as the
carrier rejects the claim by notification in writing and returns any documents handed
to him in support of the claim. If a part of the claim is admitted, the period of
limitation shall start to run again only in respect of that part of the claim still
in dispute. The burden of proof of receipt of the claim or of the reply and of the
return of the documents shall rest with the party invoking those facts. Further
claims on the same subject shall not suspend the running of the period of limitation
unless the carrier agrees to consider them.
4. Subject to the provisions of the preceding paragraph, the suspension of the
period of limitation shall be governed by the provisions of the law of the court or
tribunal seized of the case, excluding the rules relating to conflict of laws. Fresh
accrual of rights of action shall also be governed by that law.
