CHAPTER V
ACTIONS
Article 13
1. In legal proceedings arising out of carriage under this Convention, the plaintiff
may bring an action in any court or tribunal of a Contracting State designated by
agreement between the parties and, in addition, in the courts or tribunals of the
State within whose territory is situated:
(a) the place where the defendant has his principal place of business, is
habitually resident or has the place of business through which the
contract of carriage was made ; or
(b) the place where the loss or damage occurred; or
(c) the place of departure or of destination of carriage;
and in no other court or tribunal.
2. Where, in respect of a claim to which paragraph 1 of this article applies, an
action is pending before a court or tribunal competent under that paragraph or where,
in respect of such a claim, a judgement has been entered by such a court or tribunal,
no new action shall be started on the same grounds between the same parties unless
the judgement of the court or tribunal before which the first action was brought is
not enforceable in the State in which the new action is brought.
3. Where, in respect of a claim to which paragraph 1 of this article applies, a
judgement entered by a court or tribunal of a Contracting State competent under that
paragraph has become enforceable in that State, it shall become enforceable in each of
the other Contracting States as soon as the formalities required in the State
concerned have been complied with. The question of the merits of the case shall not
be reopened.
4. The provisions of the preceding paragraph shall apply to judgements after trial,
judgements by default and settlements confirmed by an order- of the court, but shall
not apply to interim judgements or to awards of damages in addition to costs against
a plaintiff who fails wholly or partly in his action.
5. Security for costs of proceedings arising out of carriage under this Convention
shall not be required from nationals of Contracting States who have their residence or
a place of business in one of those States.
