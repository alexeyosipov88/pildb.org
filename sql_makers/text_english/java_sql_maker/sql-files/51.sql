Article 6
The national law deemed applicable by the court or tribunal seized of the case
shall determine the extent of the loss or damage resulting from the death of, or
personal injury or any other bodily or mental harm caused to, a passenger, and shall
also determine which persons are entitled to compensation.
Article 7
1.
The total damages which the carrier may be'' required to pay in respect of the same
occurrence shall not exceed 200,000 francs for each victim.
2. The amount referred to in paragraph 1 of this article shall be exclusive of legal
or other costs which are incurred by the parties in asserting their rights, and which
the carrier may be required to pay or reimburse, and of interest calculated in
conformity with the law deemed applicable by the court or tribunal seized of the case.
3. A higher limit may be agreed between the parties to the contract of carriage.
Unless the contract otherwise provides, such agreement shall be for the benefit of all
persons entitled to compensation.
4. Such limitation of damages as may be established under this article shall apply
to all claims arising out of the death of, or of personal injury or any other bodily
or mental harm caused to, the passenger. Where there is more than one claimant and
the maximum compensation as established above is less than the total of the claims,
the latter shall be reduced proportionately.
Section 2. Damage to luggage
Article 8
1. . The carrier shall be liable in the event of the total or partial loss .of, or of
damage to, luggage if the loss or damage has taken place during carriage and is
attributable to the fault or neglect of the carrier or of persons for whom he is
responsible under article 2. Such fault or neglect shall be presumed, in the absence
of proof to the contrary:
(a) if the loss of or damage to the luggage has arisen from or in connexion with
shipwreck, collision, stranding, explosion or fire; or
(b) where such luggage has been handed over to the carrier, from the time when
he takes charge of it until the time either of its removal by the person
entitled to it or of its deposit in accordance with article 4«
2. Paragraph l(b) of this article shall not apply to vehicles carried on vessels or
to luggage in or on such vehicles.
3. The carrier shall be liable for the loss or damage referred to in paragraph 1
where such loss or damage,
(a) has been caused by defects in the vessel or malfunctioning of its
equipment ; or
(b) is attributable to a fault or neglect on the part of the person from whom
the carrier has hired the vessel or on the part of the persons for whom
that person, if he himself had been the carrier, would have been
responsible under article 2.
Article 9
1. The fact that a passenger has disembarked without lodging a complaint regarding
his luggage shall be prima facie evidence that he has removed the luggage complete
and in good condition. Any complaint must be made to the carrier either orally or in
writing within the seven consecutive days following the passenger''s disembarkation.
2. The passenger shall be relieved of his obligations under the preceding paragraph
if the loss or condition of the luggage has been duly checked by the passenger and
the carrier.
Article 10
1. Where luggage has disappeared and the passenger applies to the carrier for its
delivery, the luggage shall, if not delivered within fourteen days after the
application, be deemed to be lost.
2. If a piece of luggage which has been deemed to be lost is recovered within one
year following the date on which the passenger has applied for its delivery, the
carrier shall take all reasonable steps to notify the passenger. Within thirty days
following receipt of such notification, the passenger may have the piece of luggage
delivered to him either at the place of d.eparture or at the place designated for
delivery. In this case, he shall refund any compensation which may have been paid in
respect of the loss, without prejudice, however, to any claims to compensation for
damage under the terms, of this Convention, or to compensation for delay under the
terms of the applicable national law.
Article 11
1. Where under this Convention a carrier is liable for compensation in respect of
total or partial loss of, or damage to, luggage, such compensation may be claimed in
an amount equal to the amount of the loss or damage but not in excess of 5»000 francs
for each passenger; compensation may also be claimed in respect of loss of or damage
to a vehicle carried., but not in excess of 15,000 francs for each vehicle.
2.
The amounts referred, to in paragraph 1 of this article shall be exclusive of
legal or other costs which are incurred by the parties in asserting their rights, and.
which the carrier may be required, to pay or reimburse, and. of interest calculated, in
conformity with the law d.eemed applicable by the court or tribunal seized of the case.
5.
Higher limits may be agreed, between the parties to the contract of carriage.
