CHAPTER VI
NULLITY OF STIPULATIONS CONTRARY TO THE CONVENTION
Article 18
1. Any stipulation which would directly or indirectly derogate from the provisions
of this Convention shall be null and void. The nullity of such a stipulation shall
not entail the nullity of the other provisions of the contract.
2. In particular, any clause assigning to the carrier the benefit of any insurance
taken out in favour of the passenger, or any similar clause, or any clause shifting
the burden of proof, shall be null and void.
3»
Any clause attributing competence to an arbitral tribunal, if it is stipulated
prior to the event that caused the damage, shall likewise be null and void.
