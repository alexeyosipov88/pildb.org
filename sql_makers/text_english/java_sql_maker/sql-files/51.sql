CHAPTER IV
LIABILITY OF THE CARRIER
Section 1.
Personal injury
Article 3
1.
The carrier shall be liable for loss or damage resulting from the death of, or
personal injury or any other bodily or mental harm caused to, a passenger, if the
accid.ent which causes the loss or damage so suffered is connected with the carriage
and occurs while the passenger is on or is embarking on or disembarking from the
vessel, or in connexion with the loading or removal of luggage, and is attributable tr
the fault or neglect of the carrier or of persons for whom he is responsible under
article 2.
Such fault or neglect shall be presumed, in the absence of proof to the
contrary, if the death of or personal injury or other bodily or mental harm caused, to
the passenger arises from or in connexion with shipwreck, collision, stranding,
explosion or fire.
2.
The carrier shall be liable for the loss or damage referred, to in paragraph 1
where, such loss or damage,
