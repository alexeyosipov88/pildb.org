CHAPTER I
SCOPE OF APPLICATION
Article 1
1. This Convention shall apply to every contract for the carriage of passengers
and, where appropriate, of their luggage by inland waterway when, under the terms of
the contract, the carriage is to be effected on the waters of at least two States and
when the place of embarkation or the place of disembarkation, or both these places,
are situated in the territory of a Contracting State. This provision shall apply
even if the vessel used is not an inland navigation vessel and regardless of the flag,
the registration of the vessel and the place of residence or the nationality of the
.parties.
2. This Convention shall not apply if the contract of carriage is subject:
(a)
to maritime law because, carriage is by sea;
or
(b) to railway law under an international convention.
3. For the purposes of this Convention:
(a) (i) carrier means any person who as part of his trade or business
undertakes under an individual or collective contract of carriage
to carry one or more persons and, where appropriate, their luggage,
whether or not he performs the carriage himself;
(ii) passenger means any person who, in the performance of a contract of
carriage made by him or on his behalf, is carried either for reward
or free of charge by a carrier;
(b) the term luggage shall be understood to comprise any article carried under
a contract of carriage, including vehicles but not motor coaches and
lorries in commercial traffic.
4. This Convention shall apply where the contract of carriage is concluded by a
State or public authority.
