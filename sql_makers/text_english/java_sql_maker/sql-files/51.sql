a breach of the obligations of the carrier under this article.
4.
The ticket shall be prima facie evidence of the particulars shown on it.
5. Unless the ticket contains a provision to the contrary it shall, if it is not
made out in the passenger''s name, be transferable at any time before the journey
begins.
