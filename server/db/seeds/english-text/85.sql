VALUES(85, 'THE GOVERNMENTS UNDERSIGNED
CONSIDERING that it is desirable to amend the Convention for the Unification of Certain Rules 
Relating to International Carriage by Air signed at Warsaw on 12 October 1929,
HAVE AGREED as follows:
CHAPTER I
AMENDMENTS TO THE CONVENTION
Article I
In Article 1 of the Convention -
(a) paragraph 2 shall be deleted and replaced by the following:-
"2. For the purposes of this Convention, the expression international carriage means any carriage 
in which, according to the agreement between the parties, the place of departure and the place of 
destination, whether or not there be a break in the carriage or a transhipment, are situated either 
within the territories of two High Contracting Parties or within the territory of a single High 
Contracting Party if there is an agreed stopping place within the territory of another State, even 
if that State is not a High Contracting Party. Carriage between two points within the territory of 
a single High Contracting Party without an agreed stopping place within the territory of another 
State is not international carriage for the purposes of this Convention."
(b) paragraph 3 shall be deleted and replaced by the following:-
"3. Carriage to be performed by several successive air
carriers is deemed, for the purposes of this Convention, to be one undivided carriage if it has 
been regarded by the parties as a single operation, whether it had been agreed upon under the form 
of a single contract or of a series of contracts, and it does not lose its international character 
merely because one contract or a series of contracts is to be performed entirely within the 
territory of the same State."
Article II
In Article 2 of the Convention -
paragraph 2 shall be deleted and replaced by the following:-
"2. This Convention shall not apply to carriage of mail and postal packages."
Article III
In Article 3 of the Convention -
(a) paragraph 1 shall be deleted and replaced by the following:-
"1. In respect of the carriage of passengers a ticket shall be delivered containing:
(a) an indication of the places of departure and destination;
(b) if the places of departure and destination are within the territory of a single High 
Contracting Party, one or more agreed stopping places being within the
territory of another State, an indication of at least one such stopping place;
(c) a notice to the effect that, if the passenger''s journey involves an ultimate destination or 
stop in a country other than the country of departure, the Warsaw Convention may be applicable and 
that the Convention
governs and in most cases limits the liability of carriers for death or personal injury and in 
respect of loss of or damage to baggage."
(b) paragraph 2 shall be deleted and replaced by the fo1lowing:-
"2. The passenger ticket shall constitute prima facie evidence of the conclusion and conditions of 
the contract of
carriage. The absence, irregularity or loss of the passenger ticket does not affect the existence 
or the validity of the contract of carriage which shall, none the less, be subject to the rules of 
this Convention. Nevertheless, if, with the consent of the carrier, the passenger embarks without a 
passenger ticket having been delivered, or if the ticket does not include the notice required by 
paragraph 1 (c) of this Article, the carrier shall not be entitled to avail himself of the 
provisions of Article 22."
Article IV
In Article 4 of the Convention -
(a) paragraphs 1, 2 and 3 shall be deleted and replaced by the following:-
"1. In respect of the carriage of registered baggage, a
baggage check shall be delivered, which, unless combined with or incorporated in a passenger ticket 
which complies with the provisions of Article 3, paragraph 1, shall contain:
(a) an indication of the places of departure and destination;
(b) if the places of departure and destination are within the territory of a single High 
Contracting Party, one or more agreed stopping places being within the
territory of another State, an indication of at least one such stopping place;
(c) a notice to the effect that, if the carriage
involves an ultimate destination or stop in a country other than the country of departure, the 
Warsaw Convention may be applicable and that the Convention governs and in most cases limits the 
liability of carriers in respect of loss of or damage to baggage."
(b) paragraph 4 shall be deleted and replaced by the fo1lowing:-
"2. The baggage check shall constitute prima facie
evidence of the registration of the baggage and of the conditions of the contract of carriage. The 
absence, irregularity or loss of the baggage check does not affect the existence or the validity of 
the contract of carriage which shall, none the less, be subject to the rules of this Convention. 
Nevertheless, if the carrier takes charge of the baggage without a baggage check having been 
delivered or if the baggage check (unless combined with or incorporated in the passenger ticket 
which complies with the provisions of Article 3, paragraph 1 (c)) does not include the notice 
required by paragraph 1 (c) of this Article, he shall not be entitled to avail himself of the 
provisions of Article 22, paragraph 2."
Article V
In Article 6 of the Convention -
paragraph 3 shall be deleted and replaced by the following:-
"3. The carrier shall sign prior to the loading of the cargo on board the aircraft."
Article VI
Article 8 of the Convention shall be deleted and replaced by the following:-
"The air waybill shall contain:
(a) an indication of the places of departure and destination;
(b) if the places of departure and destination are within the territory of a single High 
Contracting Party, one or more agreed stopping places being within the territory of another State, 
an indication of at least one such stopping place;
(c) a notice to the consignor to the effect that, if the
carriage involves an ultimate destination or stop in a country other than the country of departure, 
the Warsaw Convention may be applicable and that the Convention governs and in most cases limits 
the liability of carriers in respect of loss of or damage to cargo."
Article VII
Article 9 of the Convention shall be deleted and replaced by the following:-
"If, with the consent of the carrier, cargo is loaded on board the aircraft without an air waybill 
having been made out, or if the air waybill does not include the notice required by Article 8, 
paragraph (c), the carrier shall not be entitled to avail himself of the provisions of Article 22, 
paragraph 2."
Article VIII
In Article 10 of the Convention -
paragraph 2 shall be deleted and replaced by the following:-
"2. The consignor shall indemnify the carrier against all damage suffered by him, or by any other 
person to whom the carrier is liable, by reason of the irregularity, incorrectness or 
incompleteness of the particulars and statements furnished by the consignor."
Article IX
To Article 15 of the Convention -
the following paragraph shall be added:-
"3. Nothing in this Convention prevents the issue of a negotiable air waybill."
Article X
Paragraph 2 of Article 20 of the Convention shall be deleted.
Article XI
Article 22 of the Convention shall be deleted and replaced by the following:-
"Article 22
1. In the carriage of persons the liability of the carrier for each passenger is limited to the sum 
of two hundred and fifty thousand francs. Where, in accordance with the law of the court seised of 
the case, damages may be awarded in the form of periodical payments, the equivalent capital value 
of the said payments shall not exceed two hundred and fifty thousand francs. Nevertheless, by 
special contract, the carrier and the passenger may agree to a higher limit of liability.
2. (a) In the carriage of registered baggage and of cargo, the
liability of the carrier is limited to a sum of two hundred and fifty francs per kilogramme, unless 
the passenger or consignor has made, at the time when the package was handed over to the carrier, a 
special declaration of interest in delivery at destination and has paid a supplementary sum if the 
case so requires. In that case the carrier will be liable to pay a sum not exceeding the declared 
sum, unless he proves that that sum is greater than the passenger''s or consignor''s actual interest 
in delivery at destination.
(b) In the case of loss, damage or delay of part of
registered baggage or cargo, or of any object contained therein, the weight to be taken into 
consideration in determining the amount to which the carrier''s liability is limited shall be only 
the total weight of the package or packages concerned. Nevertheless, when the loss, damage or delay 
of a part of the registered baggage or cargo, or of an object contained therein, affects the value 
of other packages covered by the same baggage check or the same air waybill, the total weight of 
such package or packages shall also be taken into consideration in determining the limit of 
liability.
3. As regards objects of which the passenger takes charge himself
the liability of the carrier is limited to five thousand francs per passenger.
4. The limits prescribed in this article shall not prevent the court
from awarding, in accordance with its own law, in addition, the whole or part of the court costs 
and of the other expenses of the litigation incurred by the plaintiff. The foregoing provision 
shall not apply if the amount of the damages awarded, excluding court costs and other expenses of 
the litigation, does not exceed the sum which the carrier has offered in writing to the plaintiff 
within a period of six months from the date of the occurrence causing the damage, or before the 
commencement of the action, if that is
later.
5. The sums mentioned in francs in this Article shall be deemed to refer to a currency unit 
consisting of sixty-five and a half milligrammes of
gold of millesimal fineness nine hundred. These sums may be converted into national currencies in 
round figures. Conversion of the sums into national currencies other than gold shall, in case of 
judicial proceedings, be made according to the gold value of such currencies at the date of the 
judgment."
Article XII
In Article 23 of the Convention, the existing provision shall be renumbered as paragraph 1 and 
another paragraph shall be added as follows:-
"2. Paragraph 1 of this Article shall not apply to provisions
governing loss or damage resulting from the inherent defect, quality or vice of the cargo carried."
Article XIII
In Article 25 of the Convention -
paragraphs 1 and 2 shall be deleted and replaced by the following:- "The limits of liability 
specified in Article 22 shall not apply if it is
proved that the damage resulted from an act or omission of the carrier, his servants or agents, 
done with intent to cause damage or recklessly and with knowledge that damage would probably 
result; provided that, in the case of such act or omission of a servant or agent, it is also proved 
that he was acting within the scope of his employment."
Article XIV
After Article 25 of the Convention, the following article shall be inserted:-
"Article 25 A
1. If an action is brought against a servant or agent of the carrier arising out of damage to which 
this Convention relates, such servant or agent, if he proves that he acted within the scope of his 
employment, shall
be entitled to avail himself of the limits of liability which that carrier himself is entitled to 
invoke under Article 22.
2. The aggregate of the amounts recoverable from the carrier, his servants and agents, in that 
case, shall not exceed the said limits.
3. The provisions of paragraphs 1 and 2 of this article shall not
apply if it is proved that the damage resulted from an act or omission of the servant or agent done 
with intent to cause damage or recklessly and with knowledge that damage would probably result."
Article XV
In Article 26 of the Convention -
paragraph 2 shall be deleted and replaced by the following:-
"2. In the case of damage, the person entitled to delivery must complain to the carrier forthwith 
after the discovery of the damage, and, at
the latest, within seven days from the date of receipt in the case of baggage and fourteen days 
from the date of receipt in the case of cargo. In the case of delay the complaint must be made at 
the latest within twenty-one days from the date on which the baggage or cargo have been placed at 
his disposal."
Article XVI
Article 34 of the Convention shall be deleted and replaced by the following:-
"The provisions of Articles 3 to 9 inclusive relating to documents
of carriage shall not apply in the case of carriage performed in extraordinary circumstances 
outside the normal scope of an air carrier''s business."
Article XVII
After Article 40 of the Convention, the following Article shall be inserted:-
"Article 40 A
1. In Article 37, paragraph 2 and Article 40, paragraph 1, the expression High Contracting Party 
shall mean State. In all other cases, the expression High Contracting Party shall mean a State 
whose ratification of or adherence to the Convention has become effective and whose denunciation 
thereof has not become effective.
2. For the purposes of the Convention the word territory means not
only the metropolitan territory of a State but also all other territories for the foreign relations 
of which that State is responsible."
CHAPTER II
SCOPE OF APPLICATION OF THE CONVENTION AS AMENDED
Article XVIII
The Convention as amended by this Protocol shall apply to international carriage as defined in 
Article 1 of the Convention, provided that the places of departure and destination referred to in 
that Article are situated either in the territories of two parties to this Protocol or within the 
territory of a single party to this Protocol with an agreed stopping place within the territory of 
another State.
CHAPTER III FINAL CLAUSES
Article XIX
As between the Parties to this Protocol, the Convention and the
Protocol shall be read and interpreted together as one single instrument and shall be known as the 
Warsaw Convention as amended at The Hague, 1955.
Article XX
Until the date on which this Protocol comes into force in
accordance with the provisions of Article XXII, paragraph 1, it shall remain open for signature on 
behalf of any State which up to that date has ratified or adhered to the Convention or which has 
participated in the Conference
at which this Protocol was adopted.
Article XXI
1. This Protocol shall be subject to ratification by the signatory States.
2. Ratification of this Protocol by any State which is not a Party to the Convention shall have the 
effect of adherence to the Convention as amended by this Protocol.
3. The instruments of ratification shall be deposited with the Government of the People''s Republic 
of Poland.
Article XXII
1. As soon as thirty signatory States have deposited their instru-
ments of ratification of this Protocol, it shall come into force between them on the ninetieth day 
after the deposit of the thirtieth instrument of ratification. It shall come into force for each 
State ratifying thereafter on the ninetieth day after the deposit of its instrument of 
ratification.
2. As soon as this Protocol comes into force it shall be registered with the United Nations by the 
Government of the People''s Republic of Poland.
Article XXIII
1. This Protocol shall, after it has come into force, be open for adherence by any non-signatory 
State.
2. Adherence to this Protocol by any State which is not a Party to the Convention shall have the 
effect of adherence to the Convention as amended by this Protocol.
3. Adherence shall be effected by the deposit of an instrument of adherence with the Government of 
the People''s Republic of Poland and shall take effect on the ninetieth day after the deposit.
Article XXIV
1. Any Party to this Protocol may denounce the Protocol by notification addressed to the Government 
of the People''s Republic of Poland.
2. Denunciation shall take effect six months after the date of receipt
by the Government of the People''s Republic of Poland of the notification of denunication.
3. As between the Parties to this Protocol, denunciation by any of them of the Convention in 
accordance with Article 39 thereof shall not be construed in any way as a denunciation of the 
Convention as amended by this Protocol.
Article XXV
1. This Protocol shall apply to all territories for the foreign relations
of which a State Party to this Protocol is responsible, with the exception of territories in 
respect of which a declaration has been made in accordance with paragraph 2 of this Article.
2. Any State may, at the time of deposit of its instrument of ratification or adherence, declare 
that its acceptance of this Protocol does
not apply to any one or more of the territories for the foreign relations of which such State is 
responsible.
3. Any State may subsequently, by notification to the Government
of the People''s Republic of Poland, extend the application of this Protocol to any or all of the 
territories regarding which it has made a declaration in accordance with paragraph 2 of this 
Article. The notification shall take effect on the ninetieth day after its receipt by that 
Government.
4. Any State Party to this Protocol may denounce it, in accordance with the provisions of Article 
XXIV, paragraph 1, separately for any or all
of the territories for the foreign relations of which such State is responsible.
Article XXVI
No reservation may be made to this Protocol except that a State
may at any time declare by a notification addressed to the Government of the People''s Republic of 
Poland that the Convention as amended by this Protocol shall not apply to the carriage of persons, 
cargo and baggage for
its military authorities on aircraft, registered in that State, the whole capacity of which has 
been reserved by or on behalf of such authorities.
Article XXVII
The Government of the People''s Republic of Poland shall give immediate notice to the Governments of 
all States signatories to the Convention or this Protocol, all States Parties to the Convention or 
this Protocol, and all States Members of the International Civil Aviation
Organization or of the United Nations and to the International Civil Aviation Organization:
(a) of any signature of this Protocol and the date thereof;
(b) of the deposit of any instrument of ratification or adherence in respect of this Protocol and 
the date thereof;
(c) of the date on which this Protocol comes into force in accordance with Article XXII, paragraph 
1;
(d) of the receipt of any notification of denunciation and the date thereof;
(e) of the receipt of any declaration or notification made under Article XXV and the date thereof; 
and
(f) of the receipt of any notification made under Article XXVI and the date thereof.
IN WITNESS WHEREOF the undersigned Plenipotentiaries, having been duly authorized, have signed this 
Protocol.
DONE at The Hague on the twenty-eighth day of the month of September of the year One Thousand Nine 
Hundred and Fifty-five, in three authentic texts in the English, French and Spanish languages. In 
the case of any inconsistency, the text in the French language, in which language the Convention 
was drawn up, shall prevail.
This Protocol shall be deposited with the Government of the
