Article 4
(1) Authors who are nationals of any of the
countries of the Union shall enjoy in countries
other than the country of origin of the work,
for their works, whether unpublished or first
published in a country of the Union, the rights
which their respective laws do now or may
hereafter grant to their nationals, as well as the
rights specially granted by this Convention.
(2) The enjoyment and the exercise of these
rights shall not be subject to any formality;
such enjoyment and such exercise shall be
independent of the existence of protection
in the country of origin of the work.
Consequently, apart from the provisions of this
Convention, the extent of protection, as well as
the means of redress afforded to the author to
protect his rights, shall be governed exclusively
by the laws of the country where protection is
claimed.
(3) The country of origin shall be considered
to be, in the case of published works, the country of first publication, even in the case of
works published simultaneously in several
countries of the Union which grant the same
term of protection; in the case of works published simultaneously in several countries of
the Union which grant different terms of protection, the country of which the legislation
grants the shortest term of protection. In the
case of works published simultaneously in a
country outside the Union and in a country of
the Union, the latter country shall be considered exclusively as the country of origin.
A work shall be considered as having been published simultaneously in several countries
which has been published in two or more countries within thirty days of its first publication.
(4) For the purposes of Articles 4, 5 and 6,
''published works'' shall be understood to be
works copies of which have been issued and
made available in sufficient quantities to the
public, whatever may be the means of manufacture of the copies. The presentation of a dramatic, dramatico-musical or cinematographic
work, the performance of a musical work, the
public recitation of a literary work, the transmission or the radio-diffusion of literary or
artistic works, the exhibition of a work of art
and the construction of a work of architecture
shall not constitute publication.
(5) The country of origin shall be considered to
be, in the case of unpublished works, the country to which the author belongs. However, in
the case of works of architecture, or of graphic
and plastic works forming part of a building,
the country of the Union where these works
have been built or incorporated in a building
shall be considered as the country of origin.
Article 5
Authors who are nationals of one of the
countries of the Union, and who first publish
their works in another country of the Union,
shall have in the latter country the same rights
as native authors.
Article 6
(1) Authors who are not nationals of one of
the countries of the Union, and who first publish their works in one of those countries, shall
enjoy in that country the same rights as native
authors, and in the other countries of the
Union the rights granted by this Convention.
(2) Nevertheless, where any country outside
the Union fails to protect in an adequate manner
the works of authors who are nationals of one of
the countries of the Union, the latter country
may restrict the protection given to the works of
authors who are, at the date of the first publication thereof, nationals of the other country and
are not effectively domiciled in one of the countries of the Union. If the country of first publication avails itself of this right, the other countries
of the Union shall not be required to grant to
works thus subjected to special treatment a
wider protection than that granted to them in
the country of first publication.
(3) No restrictions introduced by virtue of the
preceding paragraph shall affect the rights
which an author may have acquired in respect
of a work published in a country of the Union
before such restrictions were put into force. (4) The countries of the Union which restrict
the grant of copyright in accordance with
this Article shall give notice thereof to the
Government of the Swiss Confederation by a
written declaration specifying the countries in
regard to which protection is restricted, and the
restrictions to which rights of authors who are
nationals of those countries are subjected. The
Government of the Swiss Confederation shall
immediately communicate this declaration to
all the countries of the Union.
Article 6bis
(1) Independently of the author''s copyright,
and even after the transfer of the said copyright,
the author shall have the right, during his
lifetime, to claim authorship of the work and to
object to any distortion, mutilation or other
alteration thereof, or any other action in relation to the said work which would be prejudicial to his honour or reputation.
(2) In so far as the legislation of the countries
of the Union permits, the rights granted to the
author in accordance with the preceding paragraph shall, after his death, be maintained, at
least until the expiry of the copyright, and shall
be exercisable by the persons or institutions
authorized by the said legislation. The determination of the conditions under which the
rights mentioned in this paragraph shall be
exercised shall be governed by the legislation of
the countries of the Union.
(3) The means of redress for safeguarding the
rights granted by this Article shall be governed
by the legislation of the country where protection is claimed.
Article 7
(1) The term of protection granted by this
Convention shall be the life of the author and
fifty years after his death.
(2) However, where one or more countries of
the Union grant a term of protection in excess
of that provided by paragraph (1), the term
shall be governed by the law of the country
where protection is claimed, but shall not
exceed the term fixed in the country of origin of
the work.
(3) In the case of cinematographic and photographic works, as well as works produced by a
process analogous to cinematography or photography, and in the case of works of applied
art, the term of protection shall be governed by
the law of the country where protection is
claimed, but shall not exceed the term fixed in
the country of origin of the work.
(4) In the case of anonymous and pseudonymous works the term of protection shall be
fixed at fifty years from the date of their
publication. However, when the pseudonym
adopted by the author leaves no doubt as to his
identity, the term of protection shall be that
provided in paragraph (1). If the author of an
anonymous or pseudonymous work discloses
his identity during the above-mentioned
period, the term of protection applicable shall
be that provided in paragraph (1).
(5) In the case of posthumous works which do
not fall within the categories of works included in
paragraphs (3) and (4) the term of the protection
afforded to the heirs and the legal representatives
and assignees of the author shall end at the expiry
of fifty years after the death of the author.
(6) The term of protection subsequent to the
death of the author and the terms provided by
paragraphs (3), (4) and (5) shall run from the
date of his death or of publication, but such
terms shall always be deemed to begin on the
1st January of the year following the event
which gives rise to them.
Article 7bis
In the case of a work of joint authorship the
term of protection shall be calculated from the
date of the death of the last surviving author.
Article 8
Authors of literary and artistic works protected
by this Convention shall have the exclusive right
of making and of authorizing the translation of
their works throughout the term of protection of
their rights in the original works.
Article 9
(1) Serial novels, short stories and all other
works, whether literary, scientific or artistic,
whatever their purpose, and which are
published in the newspapers or periodicals of
one of the countries of the Union shall not be
reproduced in the other countries without the
consent of the authors.   
(2) Articles on current economic, political or
religious topics may be reproduced by the press
unless the reproduction thereof is expressly
reserved; nevertheless, the source must always
be clearly indicated. The legal consequences of
the breach of this obligation shall be determined by the laws of the country where protection is claimed.
(3) The protection of this Convention shall
not apply to news of the day nor to miscellaneous information having the character of mere
items of news.
Article 10
(1) It shall be permissible in all the countries of
the Union to make short quotations from
newspaper articles and periodicals, as well as to
include them in press summaries.
(2) The right to include excerpts from literary
or artistic works in educational or scientific
publications, or in chrestomathies, in so far as
this inclusion is justified by its purpose, shall be
a matter for legislation in the countries of the
Union, and for special Arrangements existing
or to be concluded between them.
(3) Quotations and excerpts shall be accompanied by an acknowledgment of the source
and by the name of the author, if his name
appears thereon.
Article 10 bis
It shall be a matter for legislation in countries of
the Union to determine the conditions under
which recording, reproduction, and public
communication of short extracts from literary
and artistic works may be made for the purpose
of reporting current events by means of
photography or cinematography or by radiodiffusion.
Article 11
(1) The authors of dramatic, dramaticomusical or musical works shall enjoy the
exclusive right of authorizing: (i) the public
presentation and public performance of their
works; (ii) the public distribution by any
means of the presentation and performance of
their works. The application of the provisions
of Articles 11 bis and 13 is, however, reserved.
(2) Authors of dramatic or dramatico-musical
works, during the full term of their rights over
the original works, shall enjoy the same rights
with respect to translations thereof.
(3) In order to enjoy the protection of this
Article, authors shall not be bound, when
publishing their works, to forbid the public
presentation or performance thereof.
Article 11bis
(1) Authors of literary and artistic works shall
have the exclusive right of authorizing: (i) the
radio-diffusion of their works or the communication thereof to the public by any other
means of wireless diffusion of signs, sounds or
images; (ii) any communication to the public,
whether over wires or not, of the radiodiffusion of the work, when this communication is made by a body other than the original
one; (iii) the communication to the public by
loudspeaker or any other similar instrument
transmitting, by signs, sounds or images, the
radio-diffusion of the work.
(2) It shall be a matter for legislation in the
countries of the Union to determine the conditions under which the rights mentioned in the
preceding paragraph may be exercised, but
these conditions shall apply only in the countries where they have been prescribed. They
shall not in any circumstances be prejudicial to
the moral right of the author, nor to his right to
obtain just remuneration which, in the absence
of agreement, shall be fixed by competent
authority.
(3) Except where otherwise provided, permission granted in accordance with paragraph (1)
of this Article shall not imply permission to
record the radio-diffused work by means of
instruments recording sounds or images. It
shall, however, be a matter for legislation in the
countries of the Union to determine the
regulations for ephemeral recordings made by a
broadcasting body by means of its own
facilities and used for its own emissions. The
preservation of these recordings in official
archives may, on the ground of their exceptional documentary character, be authorized
by such legislation.
Article 11ter
Authors of literary works shall enjoy the
exclusive right of authorizing the public recitation of their works.
Article 12
Authors of literary, scientific or artistic works
shall enjoy the exclusive right of authorizing
adaptations, arrangements and other alterations of their works.
Article 13
(1) Authors of musical works shall have the
exclusive right of authorizing: (i) the recording
of such works by instruments capable of reproducing them mechanically; (ii) the public performance by means of such instruments of
works thus recorded.
(2) Reservations and conditions relating to the
application of the rights mentioned in the preceding paragraph may be determined by legislation
in each country of the Union, in so far as it may be
concerned; but all such reservations and conditions shall apply only in the countries which have
prescribed them and shall not, in any circumstances, be prejudicial to the author''s right to
obtain just remuneration which, in the absence of
agreement, shall be fixed by competent authority.
(3) The provisions of paragraph (1) of this
Article shall not be retroactive and consequently shall not be applicable in a country of
the Union to works which, in that country,
may have been lawfully adapted to mechanical
instruments before the coming into force of the
Convention signed in Berlin on the 13th
November 1908, and, in the case of a country
having acceded to the Convention since that
date or acceding to it in the future, before the
date of its accession.
(4) Recordings made in accordance with paragraphs (2) and (3) of this Article and imported
without permission from the parties concerned
into a country where they are not lawfully
allowed shall be liable to seizure.
Article 14
(1) Authors of literary, scientific or artistic
works shall have the exclusive right of authorizing: (i) the cinematographic adaptation and
reproduction of these works, and the distribution of the works thus adapted or reproduced;
(ii) the public presentation and performance of
the works thus adapted or reproduced.
(2) Without prejudice to the rights of the
author of the work adapted or reproduced,
a cinematographic work shall be protected as
an original work.
(3) The adaptation under any other artistic
form of cinematographic productions derived
from literary, scientific or artistic works shall,
without prejudice to the authorization of their
authors, remain subject to the authorization of
the author of the original work.
(4) Cinematographic adaptations of literary,
scientific or artistic works shall not be subject
to the reservations and conditions contained in
Article 13, paragraph (2).
(5) The provisions of this Article shall apply to
reproduction or production effected by any
other process analogous to cinematography.
Article 14bis
(1) The author, or after his death the persons
or institutions authorized by national legislation, shall, in respect of original works of art
and original manuscripts of writers and composers, enjoy the inalienable right to an interest
in any sale of the work subsequent to the first
disposal of the work by the author.
(2) The protection provided by the preceding
paragraph may be claimed in a country of the
Union only if legislation in the country to
which the author belongs so permits, and to the
degree permitted by the country where this
protection is claimed.
(3) The procedure for collection and the
amounts shall be matters for determination by
national legislation.
Article 15
(1) In order that the author of a literary or
artistic work protected by this Convention
shall, in the absence of proof to the contrary, be
regarded as such, and consequently be entitled
to institute infringement proceedings in countries of the Union, it shall be sufficient for his
name to appear on the work in the usual manner. This paragraph shall be applicable even if
this name is a pseudonym, where the pseudonym adopted by the author leaves no doubt as
to his identity.
(2) In the case of anonymous and pseudonymous works, other than those referred to in the
preceding paragraph, the publisher whose
name appears on the work shall, in the absence
of proof to the contrary, be regarded as
representing the author, and in this capacity
he shall be entitled to protect and enforce
the author''s rights. The provisions of this
paragraph shall cease to apply if the author
reveals his identity and establishes his claim to
authorship of the work.
Article 16
(1) Works infringing copyright may be seized
by the competent authorities of any country of
the Union where the original work enjoys legal
protection.
(2) In these countries the seizure may also
apply to reproductions imported from a country where the work is not protected, or has
ceased to be protected.
(3) The seizure shall take place in accordance
with the legislation of each country.
Article 17
The provisions of this Convention cannot in
any way affect the right of the Government of
each country of the Union to permit, to control, or to prohibit by legislation or regulation,
the circulation, presentation, or exhibition of
any work or production in regard to which the
competent authority may find it necessary to
exercise that right.
Article 18
(1) This Convention shall apply to all works
which at the moment of its coming into force
have not yet fallen into the public domain in
the country of origin through the expiry of the
term of protection.
(2) If, however, through the expiry of the term
of protection which was previously granted, a
work has fallen into the public domain of the
country where protection is claimed, that work
shall not be protected anew.
(3) The application of this principle shall be in
accordance with the provisions contained in
special Conventions to that effect existing or to
be concluded between countries of the Union.
In the absence of such provisions, the respective countries shall determine, each in so far as it
is concerned, the manner in which the said
principle is to be applied.
(4) The above provisions shall apply equally in
the case of new accessions to the Union, and in
the event of protection being extended by the
application of Article 7 or by abandonment of
reservations.
Article 19
The provisions of this Convention shall not
preclude the making of a claim to the benefit of
any wider provisions which may be afforded by
legislation in a country of the Union.
Article 20
The Governments of the countries of the Union
reserve to themselves the right to enter into special Arrangements between each other, in so far
as such Arrangements shall confer upon authors
more extended rights than those granted by the
Convention, or embody other provisions not
contrary to this Convention. The provisions of
existing Arrangements which satisfy these conditions shall remain applicable.
Article 21
(1) The International Office established under
the name of the ''Office of the International
Union for the Protection of Literary and
Artistic Works'' shall be maintained.
(2) That Office shall be placed under the high
authority of the Government of the Swiss
Confederation, which shall regulate its organization and supervise its working.
(3) The official language of the Office shall be
the French language.
Article 22
(1) The International Office shall collect
information of every kind relating to the protection of the rights of authors over their literary and artistic works. It shall co-ordinate and
publish such information. It shall undertake
the study of questions of general interest to the
Union and, by the aid of documents placed at
its disposal by the different Administrations, it
shall edit a periodical publication in the French
language on questions which concern the purpose of the Union. The Governments of the
countries of the Union reserve to themselves
the power to authorize by agreement the publication by the Office of an edition in one or
more other languages if, by experience, this
should be shown to be necessary.
(2) The International Office shall always place
itself at the disposal of members of the Union in order to provide them with any special
information which they may require relating to
the protection of literary and artistic works.
(3) The Director of the International Office
shall make an annual report on his administration, which shall be communicated to all the
members of the Union.
