VALUES(99, 'Article 1
The countries to which this Convention
applies constitute a Union for the protection of
the rights of authors over their literary and
artistic works.
Article 2
(1) The term ''literary and artistic works'' shall
include every production in the literary, scientific and artistic domain, whatever may be
the mode or form of its expression, such as
books, pamphlets and other writings; lectures,
addresses, sermons and other works of the same
nature; dramatic or dramatico-musical works;
choreographic works and entertainments in
dumb show, the acting form of which is fixed in
writing or otherwise; musical compositions with
or without words; cinematographic works and
works produced by a process analogous to cinematography; works of drawing, painting, architecture, sculpture, engraving and lithography;
photographic works and works produced by a
process analogous to photography; works of
applied art; illustrations, geographical charts,
plans, sketches and plastic works relative to
geography, topography, architecture or science.
(2) Translations, adaptations, arrangements
of music and other alterations of a literary or
artistic work shall be protected as original
works without prejudice to the rights of the
author of the original work. It shall, however,
be a matter for legislation in the countries of
the Union to determine the protection to be
granted to translations of official texts of a
legislative, administrative and legal nature.
(3) Collections of literary or artistic works
such as encyclopaedias and anthologies which
by reason of the selection and arrangement of
their contents constitute intellectual creations
shall be protected as such without prejudice to
the rights of the authors in respect of each of
the works forming part of such collections.
(4) The works mentioned in this Article shall
enjoy protection in all countries of the Union.
This protection shall operate for the benefit of the
author and his legal representatives and assignees.
(5) It shall be a matter for legislation in the
countries of the Union to determine the extent
of the application of their laws to works of
applied art and industrial designs and models,
as well as the conditions under which such
works, designs and models shall be protected.
Works protected in the country of origin solely
as designs and models shall be entitled in other
countries of the Union only to such protection
as shall be accorded to designs and models in
such countries.
Article 2bis
(1) It shall be a matter for legislation in the
countries of the Union to exclude wholly or in
part from the protection afforded by the preceding Article political speeches and speeches
delivered in the course of legal proceedings.
(2) It shall also be a matter for legislation in
the countries of the Union to determine the conditions under which lectures, addresses,
sermons and other works of the same nature
may be reproduced by the press.
(3) Nevertheless, the author alone shall have
the right of making a collection of his works
mentioned in the above paragraphs.
