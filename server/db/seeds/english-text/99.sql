Article 24
(1) This Convention may be submitted to
revision for the purpose of introducing
improvements intended to perfect the system
of the Union.
(2) Questions of this kind, as well as those
which in other respects concern the development of the Union, shall be considered in
Conferences to be held successively in
the countries of the Union by delegates of the
said countries. The Administration of the
country where a Conference is to meet shall,
with the assistance of the International Office,
prepare the programme of the Conference. The
Director of the Office shall attend the sessions
of the Conferences, and may take part in the
discussions, but without the right to vote.
(3) No alteration in this Convention shall be
binding on the Union except by the unanimous consent of the countries composing it.
Article 25
(1) Countries outside the Union which make
provision for the legal protection of the rights
forming the object of this Convention may
accede thereto upon request.
(2) Such accession shall be notified in writing
to the Government of the Swiss Confederation,
who shall communicate it to all the other
countries of the Union.
(3) Such accession shall imply full acceptance
of all the clauses and admission to all the advantages provided by this Convention, and shall
take effect one month after the date of the notification made by the Government of the Swiss
Confederation to the other countries of the
Union, unless some later date has been indicated by the acceding country. It may, nevertheless, contain an indication that the acceding
country wishes to substitute, provisionally at
least, for Article 8, which relates to translations,
the provisions of Article 5 of the Convention of
1886 revised at Paris in 1896, on the understanding that those provisions shall apply only
to translations into the language or languages
of that country.
Article 26
(1) Any country of the Union may at any time
in writing notify the Swiss Government that this Convention shall apply to its overseas territories, colonies, protectorates, territories
under its trusteeship, or to any other territory
for the international relations of which it is
responsible, and the Convention shall thereupon apply to all the territories named in such
notification, as from a date determined in
accordance with Article 25, paragraph (3). In
the absence of such notification, the Convention
shall not apply to such territories.
(2) Any country of the Union may at any time
in writing notify the Government of the Swiss
Confederation that this Convention shall cease
to apply to all or any of the territories which
have been made the subject of a notification
under the preceding paragraph, and the
Convention shall cease to apply in the territories named in such notification twelve
months after its receipt by the Government of
the Swiss Confederation.
(3) All notifications given to the Government
of the Swiss Confederation in accordance with
the provisions of paragraph (1) and (2) of
this Article shall be communicated by that
Government to all the countries of the Union.
Article 27
(1) This Convention shall replace, in relations
between the countries of the Union, the
Convention of Berne of the 9th September
1886, and the subsequent revisions thereof.
The Instruments previously in force shall continue to be applicable in relations with countries which do not ratify this Convention.
(2) The countries on whose behalf this
Convention is signed may retain the benefit of
the reservations which they have previously
formulated, on condition that they make a
declaration to that effect at the time of the
deposit of their ratifications.
(3) Countries which are at present members
of the Union, but on whose behalf this
Convention is not signed, may accede to it at
any time, in the manner provided for in Article
25. In that event they shall enjoy the benefit of
the provisions of the preceding paragraph.
Article 27 bis
A dispute between two or more countries of
the Union concerning the interpretation or
application of this Convention, not settled by
negotiation, shall be brought before the
International Court of Justice for determination by it, unless the countries concerned agree
on some other method of settlement. The
country requesting that the dispute should
be brought before the Court shall inform the
International Office; the Office shall bring the
matter to the attention of the other countries of
the Union.
Article 28
(1) This Convention shall be ratified, and
the ratifications deposited at Brussels, not
later than the 1st July 1951. The ratifications,
with the dates thereof and all declarations
which may accompany them, shall be communicated by the Belgian Government to the
Government of the Swiss Confederation,
which shall notify the other countries of the
Union thereof.
(2) This Convention shall come into force,
between the countries which have ratified it,
one month after the 1st July 1951.
Nevertheless, if before that date it has been ratified by at least six countries of the Union, it
shall come into force between those countries
one month after the notification to them by
the Government of the Swiss Confederation of
the deposit of the sixth ratification and, in the
case of countries which ratify thereafter, one
month after the notification of each of such
ratifications.
(3) Until the 1st July 1951, countries outside
the Union may join it by acceding either to
the Convention signed at Rome on the 2nd
June 1928, or to this Convention. On or after
the 1st July 1951, they may accede only to this
Convention. The countries of the Union
which shall not have ratified this Convention
by the 1st July 1951 may accede thereto in
accordance with the procedure provided by
Article 25. In this event they shall be entitled to
the benefit of the provisions of Article 27, paragraph (2).
Article 29
(1) This Convention shall remain in force for
an indefinite period. Nevertheless, each country of the Union shall be entitled to denounce it
at any time, by means of a notification in writing addressed to the Government of the
Swiss Confederation.
(2) This denunciation, which shall be communicated by the Government of the Swiss
Confederation to all the other countries of the
Union, shall take effect only in respect of
the country making it, and twelve months after
the receipt of the notification of denunciation
addressed to the Government of the Swiss
Confederation. The Convention shall remain
in full force and effect for the other countries of
the Union.
(3) The right of denunciation provided by this
Article shall not be exercised by any country
before the expiry of five years from the date of
its ratification or accession.
Article 30
(1) Countries which introduce into their legislation the term of protection of fifty years provided by Article 7, paragraph (1), of this
Convention shall give notice thereof in writing
to the Government of the Swiss Confederation,
which shall immediately communicate it to all
the other countries of the Union.
(2) The same procedure shall be followed in
the case of countries abandoning the reservations made or maintained by them in accordance with Articles 25 and 27.
Article 31
The official Acts of the Conferences shall be
established in French. An equivalent text shall
be established in English. In case of dispute as
to the interpretation of the Acts, the French
text shall always prevail. Any country or group
of countries of the Union shall be entitled to
have established by the International Office an
authoritative text of the said Acts in the language of its choice, and by arrangement with
the Office. These texts shall be published in the
Acts of the Conferences, annexed to the French
and English texts.
